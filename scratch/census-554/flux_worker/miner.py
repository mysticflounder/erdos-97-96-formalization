#!/usr/bin/env python3
"""Pattern mining + certificate emission for the (5,5,4) census CEGAR loop.

STANDALONE COPY (scratch/census-554/flux_worker/miner.py): forked from
scratch/census-554/miner.py for the flux remote-certify-worker add-on. The
only change vs. the original is the SCRATCH path below (portable across
machines/sessions instead of one Claude session's ephemeral scratchpad). Do
not let this copy drift silently from the original's certify_pattern logic.

A PATTERN is a dict {center c: frozenset M_c} with |M_c| >= 2, c not in M_c,
meaning "K_c ⊇ M_c for every listed center".  Its ideal is generated by the
equidistance polys  d2(c, m0) - d2(c, m)  (m0 = min M_c) for each listed
center.  A cube contains the pattern iff M_c ⊆ K_c for all c; then every
pattern generator is a Z-linear combination of the cube's generators, so a
pattern kill kills every containing cube.

Pattern DEADNESS (equality-only, checker-compatible):
  dead  ⟺  the system {pattern polys} ∪ {t_j·d2(x_j,y_j) − 1 : all pairs
           (x_j,y_j) of pattern points}  is ℂ-empty
        ⟺  no ℂ-assignment satisfies the pattern equalities with all pattern
           points pairwise distinct.
  A real (5,5,4) configuration containing the pattern would give exactly such
  an assignment (points distinct as Finset members, t_j := 1/d2 real), so
  1 ∈ ideal  evaluates to  1 = 0.  No ℝ-inequality content anywhere.

Fast oracle: msolve char-0 ([-1] ⟺ empty).  Every BANKED kill is re-confirmed
exact by Singular reduce(1,std)==0 and equipped with lift cofactors, then the
Nullstellensatz identity  Σ coeff_i · gen_i = 1  is re-verified in exact
Fraction arithmetic in python (independent of Singular).
"""

