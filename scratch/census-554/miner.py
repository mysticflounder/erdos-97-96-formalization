# ---------------- pattern algebra ----------------

def pattern_points(pat):
    pts = set()
    for c, M in pat.items():
        pts.add(c)
        pts.update(M)
    return sorted(pts)


def pattern_polys(pat):
    """Equidistance generators of the pattern (list of L-Polys), with tags."""
    polys, tags = [], []
    for c in sorted(pat):
        M = sorted(pat[c])
        m0 = M[0]
        base = L.d2poly(c, m0)
        for m in M[1:]:
            p = L.psub(base, L.d2poly(c, m))
            if p:
                polys.append(p)
                tags.append((c, m0, m))
    return polys, tags


def rabinowitsch_polys(pat, pairs=None):
    """t_j·d2(x,y) − 1 for the given pairs (default: all pattern-point pairs
    with nonconstant d2 — the gauge pair (0,1) has d2 ≡ 1, never a kill)."""
    pts = pattern_points(pat)
    if pairs is None:
        pairs = [e for e in combinations(pts, 2) if e != (0, 1)]
    out = []
    for j, (a, b) in enumerate(pairs):
        d = L.d2poly(a, b)
        # t_j gets a fresh variable index beyond the 18 census vars
        tvar = len(L.VARS) + j
        t = {((tvar, 1),): Fraction(1)}
        out.append((L.padd(L.pmul(t, d), {(): Fraction(-1)}), (a, b), tvar))
    return out


def used_vars(polys):
    vs = set()
    for p in polys:
        for m in p:
            for i, _ in m:
                vs.add(i)
    return sorted(vs)


def var_name(i, tnames):
    return L.VARS[i] if i < len(L.VARS) else tnames[i]


# ---------------- oracles ----------------

def msolve_empty(polys, timeout=30):
    """True iff msolve char-0 reports the ideal ℂ-empty ([-1])."""
    vs = used_vars(polys)
    tnames = {}
    for i in vs:
        if i >= len(L.VARS):
            tnames[i] = f"t{i - len(L.VARS)}"
    names = [var_name(i, tnames) for i in vs]
    remap = {i: k for k, i in enumerate(vs)}
    strs = []
    for p in polys:
        q = {tuple(sorted((remap[i], e) for i, e in m)): c for m, c in p.items()}
        strs.append(L.poly_str(q, varnames=names))
    inp = ",".join(names) + "\n0\n" + ",\n".join(strs) + "\n"
    with tempfile.NamedTemporaryFile("w", suffix=".ms", dir=SCRATCH,
                                     delete=False) as f:
        f.write(inp)
        path = f.name
    out = path + ".out"
    try:
        subprocess.run(["msolve", "-f", path, "-o", out, "-t", "1"],
                       capture_output=True, text=True, timeout=timeout)
        raw = open(out).read().strip()
        return raw.startswith("[-1]")
    except subprocess.TimeoutExpired:
        return False
    finally:
        for q in (path, out):
            try:
                os.unlink(q)
            except OSError:
                pass


def pattern_dead_fast(pat, timeout=30):
    """msolve oracle: pattern equalities + all-pairs Rabinowitsch ℂ-empty."""
    polys, _ = pattern_polys(pat)
    if not polys:
        return False
    rabs = rabinowitsch_polys(pat)
    return msolve_empty(polys + [r[0] for r in rabs], timeout=timeout)


# ---------------- mining ----------------

def induced_pattern(cube, P):
    Ps = set(P)
    pat = {}
    for c in P:
        M = frozenset(cube[c]) & Ps
        if len(M) >= 2:
            pat[c] = frozenset(M)
    return pat


def n_gens(pat):
    return sum(len(M) - 1 for M in pat.values())


def greedy_shrink(pat, timeout=20):
    """Greedy member/center removal while still msolve-dead."""
    pat = {c: frozenset(M) for c, M in pat.items()}
    improved = True
    while improved:
        improved = False
        for c in sorted(pat, key=lambda c: -len(pat[c])):
            for m in sorted(pat[c]):
                trial = dict(pat)
                M = pat[c] - {m}
                if len(M) >= 2:
                    trial[c] = M
                else:
                    del trial[c]
                if not trial:
                    continue
                if pattern_dead_fast(trial, timeout):
                    pat = trial
                    improved = True
                    break
            if improved:
                break
    return pat


def canon_key(pat):
    """Canonical form of a pattern under AUTOS (min over the 72 images)."""
    best = None
    for m in L.AUTOS:
        img = tuple(sorted((m[c], tuple(sorted(m[x] for x in M)))
                           for c, M in pat.items()))
        if best is None or img < best:
            best = img
    return best


def instance_subsumes(img, pat):
    """True iff banning `img` already bans `pat`: every cube containing
    pattern `pat` contains pattern `img` (img[c] ⊆ pat[c] centerwise)."""
    return all(c in pat and M <= pat[c] for c, M in img.items())


def _dead_supports_min_k(cube, log):
    """Phase 1: all msolve-dead induced patterns at the smallest point-support
    size k (k = 4..N; k = N is the full cube).  Returns (dead_list, k)."""
    from concurrent.futures import ThreadPoolExecutor
    for k in range(4, L.N + 1):
        cands = []
        for P in combinations(range(L.N), k):
            pat = induced_pattern(cube, P)
            npolys = n_gens(pat)
            if npolys >= max(3, 2 * k - 5):
                cands.append(pat)
        if not cands:
            continue
        with ThreadPoolExecutor(max_workers=12) as ex:
            flags = list(ex.map(lambda p: pattern_dead_fast(p, 20), cands))
        dead = [p for p, f in zip(cands, flags) if f]
        if dead:
            log(f"    point-support hit at k={k} "
                f"({len(dead)}/{len(cands)} dead)")
            return dead, k
    return [], None


def mine_all_patterns(cube, log=lambda *a: None):
    """All msolve-dead min-k induced patterns of the cube, each greedy-shrunk,
    deduped by AUTOS-canonical form.  Returns (patterns, k); ([], None) when
    no induced pattern at any k <= N (incl. the full cube) is msolve-dead —
    caller must then run the exact engine as an ALIVE screen."""
    from concurrent.futures import ThreadPoolExecutor
    dead, k = _dead_supports_min_k(cube, log)
    if not dead:
        return [], None
    dead.sort(key=lambda p: (n_gens(p), len(p)))
    with ThreadPoolExecutor(max_workers=12) as ex:
        shrunk = list(ex.map(greedy_shrink, dead))
    out, seen = [], set()
    for q in sorted(shrunk, key=lambda p: (n_gens(p), len(p))):
        ck = canon_key(q)
        if ck in seen:
            continue
        seen.add(ck)
        out.append(q)
    if len(out) != len(dead):
        log(f"    shrunk/deduped -> {len(out)} canonical patterns")
    return out, k


def mine_pattern(cube, log=lambda *a: None):
    """Return ONE small dead pattern contained in the dead cube (legacy path;
    the CEGAR loop uses mine_all_patterns).

    Phase 1: smallest point-support induced patterns (k = 4..) that are dead.
    Phase 2: greedy member/center removal while still dead (msolve oracle).
    Final confirmation is done separately by certify_pattern (Singular exact).
    """
    dead, _k = _dead_supports_min_k(cube, log)
    if dead:
        start = min(dead, key=lambda p: (n_gens(p), len(p)))
    else:
        start = {c: frozenset(cube[c]) for c in range(L.N)}
        log("    no small point-support core; starting greedy from full cube")
    return greedy_shrink(start)


# ---------------- exact certification (Singular lift + python re-check) ----

def sing_run(script, timeout=300):
    try:
        pr = subprocess.run(["Singular", "-q"], input=script,
                            capture_output=True, text=True, timeout=timeout)
        return pr.stdout
    except subprocess.TimeoutExpired:
        return None


def parse_poly_str(s, name_to_idx):
    """Parse a Singular expanded polynomial string into an L-Poly."""
    s = s.strip().replace(" ", "")
    if s in ("0", ""):
        return {}
    # term splitting on top-level + and -
    terms, cur = [], ""
    for ch in s:
        if ch in "+-" and cur and cur[-1] not in "+-*/^(":
            terms.append(cur)
            cur = ch
        else:
            cur += ch
    terms.append(cur)
    poly = {}
    for t in terms:
        sign = Fraction(1)
        while t and t[0] in "+-":
            if t[0] == "-":
                sign = -sign
            t = t[1:]
        coeff = sign
        monom = {}
        for f in t.split("*"):
            if not f:
                continue
            if f[0].isdigit():
                coeff *= Fraction(f)
            else:
                if "^" in f:
                    v, e = f.split("^")
                    e = int(e)
                else:
                    v, e = f, 1
                monom[name_to_idx[v]] = monom.get(name_to_idx[v], 0) + e
        key = tuple(sorted(monom.items()))
        poly[key] = poly.get(key, Fraction(0)) + coeff
    return {m: c for m, c in poly.items() if c}


def certify_pattern(pat, timeout=240):
    """Exact certificate for a dead pattern.  Tries, in order:
      base kill:  1 ∈ ⟨pattern polys⟩
      pair kill:  1 ∈ ⟨pattern polys + one Rabinowitsch⟩   (each pair)
      multi kill: 1 ∈ ⟨pattern polys + all Rabinowitsch⟩
    Returns payload dict or None.  Identity re-verified in exact Fractions."""
    polys, tags = pattern_polys(pat)

    def attempt(rabs):
        gens = polys + [r[0] for r in rabs]
        vs = used_vars(gens)
        tnames = {i: f"t{i - len(L.VARS)}" for i in vs if i >= len(L.VARS)}
        names = [var_name(i, tnames) for i in vs]
        remap = {i: k for k, i in enumerate(vs)}

        def render(p):
            q = {tuple(sorted((remap[i], e) for i, e in m)): c
                 for m, c in p.items()}
            return L.poly_str(q, varnames=names)

        gstrs = [render(g) for g in gens]
        script_lines = [
            f"ring r = 0, ({','.join(names)}), dp;",
            "ideal I = " + ",\n  ".join(gstrs) + ";",
            "ideal G = std(I);",
            'string("RED1:", reduce(1, G));',
            "matrix T = lift(I, ideal(1));",
            "poly chk = 0;",
            "int i;",
            "for (i = 1; i <= size(I); i = i + 1) { chk = chk + I[i]*T[i,1]; }",
            'string("CHK:", chk);',
            "for (i = 1; i <= size(I); i = i + 1) "
            '{ string("COF", i, ":", T[i,1]); }',
            "exit;"]
        out = sing_run("\n".join(script_lines), timeout)
        if out is None or "RED1:0" not in out:
            return None
        if "CHK:1" not in out:
            return None
        cofs = {}
        for ln in out.splitlines():
            ln = ln.strip()
            if ln.startswith("COF"):
                k, val = ln[3:].split(":", 1)
                cofs[int(k)] = val
        if len(cofs) != len(gens):
            return None
        name_to_idx = {n: k for k, n in enumerate(names)}
        coeff_polys = [parse_poly_str(cofs[i + 1], name_to_idx)
                       for i in range(len(gens))]
        gen_polys = [
            {tuple(sorted((remap[i], e) for i, e in m)): c for m, c in g.items()}
            for g in gens]
        # exact python identity re-check
        acc = {}
        for cp, gp in zip(coeff_polys, gen_polys):
            acc = L.padd(acc, L.pmul(cp, gp))
        if acc != {(): Fraction(1)}:
            return None
        return {
            "variables": names,
            "generators": [render(g) for g in gens],
            "generator_tags": ([list(t) for t in tags] +
                               [["rab", r[1][0], r[1][1]] for r in rabs]),
            "coefficients": [cofs[i + 1] for i in range(len(gens))],
            "python_exact_identity": True,
            "rab_pairs": [list(r[1]) for r in rabs],
        }

    payload = attempt([])
    kill = "base"
    if payload is None:
        pts = pattern_points(pat)
        pairs = [e for e in combinations(pts, 2) if e != (0, 1)]
        # shrink the Rabinowitsch pair set with the CHEAP msolve oracle FIRST
        # (mining already established full-pair-set emptiness), then lift the
        # SHRUNK system — far fewer t-vars keeps Singular's lift tractable.
        # All-pairs lift is only the fallback.  Sound either way: whatever
        # payload is kept was lift-verified and re-checked in exact Fractions.
        cur = list(pairs)
        for e in list(cur):
            if len(cur) == 1:
                break
            trial = [p for p in cur if p != e]
            rabs = rabinowitsch_polys(pat, pairs=trial)
            if msolve_empty(polys + [r[0] for r in rabs], timeout=10):
                cur = trial
        payload = attempt(rabinowitsch_polys(pat, pairs=cur))
        if payload is not None:
            pairs = cur
        else:
            payload = attempt(rabinowitsch_polys(pat, pairs=pairs))
        kill = (f"pair:{pairs[0][0]}-{pairs[0][1]}" if len(pairs) == 1
                else "multi_pair")
    if payload is None:
        return None
    payload["schema"] = "census554_pattern_certificate.v1"
    payload["kill"] = kill
    payload["pattern"] = {str(c): sorted(pat[c]) for c in sorted(pat)}
    payload["identity"] = "sum_i coefficients[i]*generators[i] = 1"
    payload["gauge"] = "pt0=(0,0), pt1=(1,0)"
    return payload


# ---------------- orbit closure ----------------

def orbit(pat):
    """All distinct AUTOS images of the pattern."""
    out = {}
    for m in L.AUTOS:
        img = {}
        for c, M in pat.items():
            img[m[c]] = frozenset(m[x] for x in M)
        key = tuple(sorted((c, tuple(sorted(M))) for c, M in img.items()))
        out[key] = img
    return list(out.values())


def contains(cube, pat):
    return all(pat[c] <= frozenset(cube[c]) for c in pat)


