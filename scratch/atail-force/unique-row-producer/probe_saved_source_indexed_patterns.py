def require(condition: bool, message: str) -> None:
    if not condition:
        raise AssertionError(message)


def load_oracle():
    spec = importlib.util.spec_from_file_location("unique_row_metric_oracle", ORACLE_PATH)
    require(spec is not None and spec.loader is not None, "cannot load metric oracle")
    module = importlib.util.module_from_spec(spec)
    sys.modules[spec.name] = module
    spec.loader.exec_module(module)
    return module


def singular_polynomial(poly: sp.Poly) -> str:
    return str(poly.as_expr()).replace("**", "^").replace(" ", "")


def parse_pair(text: str) -> tuple[int, int]:
    pair = tuple(int(item) for item in text.split(","))
    require(len(pair) == 2 and 0 <= pair[0] < pair[1] < 12, "invalid pair")
    return pair


def exact_replay(
    coefficient_text: Sequence[str],
    polynomials: Sequence[sp.Poly],
    target: sp.Poly,
) -> bool:
    symbols = polynomials[0].gens
    # ``run_singular`` may declare the same coordinate variables in reverse
    # order to change the Groebner monomial order.  SymPy's ``Poly`` equality
    # is generator-order-sensitive, so comparing the canonical-gens sum to a
    # reversed-gens target falsely rejects a valid identity.  Normalize the
    # target into the generator tuple used by the input polynomials before the
    # exact comparison.
    normalized_target = sp.Poly(target.as_expr(), *symbols, domain=sp.QQ)
    symbol_index = {str(symbol): index for index, symbol in enumerate(symbols)}

    def parse_expanded(text: str) -> sp.Poly:
        compact = text.replace(" ", "")
        if compact == "0":
            return sp.Poly(0, *symbols, domain=sp.QQ)
        require(
            "(" not in compact and ")" not in compact,
            "exact replay requires expanded coefficients",
        )
        terms: dict[tuple[int, ...], sp.Rational] = {}
        for raw_term in re.findall(r"[+-]?[^+-]+", compact):
            sign = -1 if raw_term.startswith("-") else 1
            body = raw_term[1:] if raw_term[:1] in "+-" else raw_term
            coefficient = sp.Rational(sign)
            exponents = [0] * len(symbols)
            for factor in body.split("*"):
                base, separator, exponent_text = factor.partition("^")
                if base in symbol_index:
                    exponents[symbol_index[base]] += (
                        int(exponent_text) if separator else 1
                    )
                else:
                    require(not separator, f"unexpected powered scalar: {factor}")
                    coefficient *= sp.Rational(base)
            key = tuple(exponents)
            terms[key] = terms.get(key, sp.Rational(0)) + coefficient
        return sp.Poly.from_dict(terms, symbols, domain=sp.QQ)

    coefficients = [parse_expanded(text) for text in coefficient_text]
    total = sp.Poly(0, *symbols, domain=sp.QQ)
    for coefficient, polynomial in zip(coefficients, polynomials, strict=True):
        total += coefficient * polynomial
    return total == normalized_target


def exact_replay_smoke_gates() -> None:
    """Guard the generator-order normalization and a mutated identity."""

    left, right = sp.symbols("replay_left replay_right")
    generators = [sp.Poly(1, left, right, domain=sp.QQ)]
    reversed_target = sp.Poly(1, right, left, domain=sp.QQ)
    require(
        exact_replay(["1"], generators, reversed_target),
        "reversed-generator exact replay smoke gate failed",
    )
    require(
        not exact_replay(["2"], generators, reversed_target),
        "mutated exact replay smoke gate failed",
    )


def run_msolve_raw(
    oracle,
    variables: Sequence[str],
    polynomials: Sequence[str],
    *,
    timeout_seconds: float,
    include_output: bool,
) -> dict[str, object]:
    try:
        with tempfile.TemporaryDirectory(prefix="p97-unique-row-msolve-") as temp:
            input_path = Path(temp) / "system.ms"
            output_path = Path(temp) / "output.txt"
            input_path.write_text(
                oracle._msolve_input(variables, polynomials), encoding="utf-8"
            )
            process = subprocess.run(
                [
                    "msolve", "-f", str(input_path), "-o", str(output_path),
                    "-t", "1",
                ],
                capture_output=True,
                text=True,
                timeout=timeout_seconds,
                check=False,
            )
            output = (
                output_path.read_text(encoding="utf-8")
                if output_path.exists()
                else ""
            )
    except subprocess.TimeoutExpired:
        return {"verdict": "TIMEOUT"}
    result: dict[str, object] = {
        "verdict": oracle.parse_msolve_verdict(output),
        "returncode": process.returncode,
        "output_char_count": len(output),
        "output_sha256": hashlib.sha256(output.encode("utf-8")).hexdigest(),
        "stderr": process.stderr,
    }
    if include_output:
        result["output"] = output
    return result


def run_singular(
    oracle,
    rows,
    *,
    timeout_seconds: float,
    pairs: Sequence[tuple[int, int]],
    lift_pair: tuple[int, int] | None,
    lift_unit: bool,
    include_lift: bool,
    reverse_variables: bool = False,
) -> dict[str, object]:
    canonical_variables = oracle.variable_symbols(12)
    variables = (
        tuple(reversed(canonical_variables))
        if reverse_variables
        else canonical_variables
    )
    polynomials = oracle.system_polynomials(12, rows)
    serialized = [oracle.serialize_poly(poly) for poly in polynomials]
    points = oracle.coordinate_symbols(12)
    pair_polynomials = {
        pair: sp.Poly(
            oracle.squared_distance(points, *pair),
            *variables,
            domain=sp.QQ,
        )
        for pair in pairs
    }
    lines = [
        f"ring R=0,({','.join(map(str, variables))}),dp;",
        f"ideal I={','.join(serialized)};",
        "ideal G=std(I);",
        'if (size(G)==1 and G[1]==1) { print("STATUS_UNIT"); }',
        'else { print("STATUS_NONUNIT"); print("DIM="+string(dim(G))); '
        'print("BASIS_SIZE="+string(size(G))); }',
    ]
    for pair, polynomial in pair_polynomials.items():
        lines.append(
            f'if (reduce({singular_polynomial(polynomial)},G)==0) '
            f'{{ print("FORCED_ZERO_{pair[0]}_{pair[1]}"); }}'
        )
    require(not (lift_pair is not None and lift_unit), "choose one lift target")
    lift_target: sp.Poly | None = None
    lift_kind: str | None = None
    if lift_pair is not None:
        lift_kind = "squared-distance"
        lift_target = sp.Poly(
            oracle.squared_distance(points, *lift_pair),
            *variables,
            domain=sp.QQ,
        )
    elif lift_unit:
        lift_kind = "unit"
        lift_target = sp.Poly(1, *variables, domain=sp.QQ)
    if lift_target is not None:
        lines.extend([
            f"ideal T={singular_polynomial(lift_target)};",
            "matrix L=lift(I,T);",
        ])
        lines.extend(
            f'print("LIFT_{index}="+string(L[{index},1]));'
            for index in range(1, len(polynomials) + 1)
        )
        lines.extend([
            "poly C=0;",
            *(
                f"C=C+L[{index},1]*I[{index}];"
                for index in range(1, len(polynomials) + 1)
            ),
            f"poly D=C-({singular_polynomial(lift_target)});",
            'print("LIFT_CHECK="+string(D));',
        ])
    lines.append("quit;")
    try:
        process = subprocess.run(
            ["Singular", "-q"],
            input="\n".join(lines) + "\n",
            capture_output=True,
            text=True,
            timeout=timeout_seconds,
            check=False,
        )
    except subprocess.TimeoutExpired:
        return {"status": "TIMEOUT", "timeout_seconds": timeout_seconds}
    output = [line.strip() for line in process.stdout.splitlines() if line.strip()]
    status = next(
        (line.removeprefix("STATUS_") for line in output if line.startswith("STATUS_")),
        "ERROR",
    )
    result: dict[str, object] = {
        "status": status,
        "returncode": process.returncode,
        "stderr": process.stderr,
        "dimension": next(
            (int(line.removeprefix("DIM=")) for line in output if line.startswith("DIM=")),
            None,
        ),
        "basis_size": next(
            (int(line.removeprefix("BASIS_SIZE=")) for line in output
             if line.startswith("BASIS_SIZE=")),
            None,
        ),
        "forced_zero_squared_distance_pairs": [
            [int(item) for item in line.removeprefix("FORCED_ZERO_").split("_")]
            for line in output if line.startswith("FORCED_ZERO_")
        ],
    }
    if lift_target is not None:
        coefficient_text = [
            line.split("=", 1)[1]
            for line in output
            if line.startswith("LIFT_") and not line.startswith("LIFT_CHECK")
        ]
        replay = (
            len(coefficient_text) == len(polynomials)
            and exact_replay(coefficient_text, polynomials, lift_target)
        )
        lift: dict[str, object] = {
            "kind": lift_kind,
            "pair": list(lift_pair) if lift_pair is not None else None,
            "coefficient_count": len(coefficient_text),
            "nonzero_coefficient_count": sum(value != "0" for value in coefficient_text),
            "singular_check": "LIFT_CHECK=0" in output,
            "exact_sympy_replay": replay,
            "coefficient_stream_sha256": hashlib.sha256(
                "\n".join(coefficient_text).encode("utf-8")
            ).hexdigest(),
        }
        if include_lift:
            lift.update({
                "target": singular_polynomial(lift_target),
                "generators": serialized,
                "coefficients": coefficient_text,
            })
        result["lift"] = lift
    return result


