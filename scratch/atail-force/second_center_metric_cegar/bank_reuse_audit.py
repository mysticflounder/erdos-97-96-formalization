def sha256_file(path: Path) -> str:
    return hashlib.sha256(path.read_bytes()).hexdigest()


def canonical_sha256(value: object) -> str:
    payload = json.dumps(value, sort_keys=True, separators=(",", ":")).encode()
    return hashlib.sha256(payload).hexdigest()


def edge(left: int, right: int) -> tuple[int, int]:
    return (left, right) if left <= right else (right, left)


class EdgeClosure:
    def __init__(self, labels: Sequence[int]) -> None:
        self.parent = {
            edge(left, right): edge(left, right)
            for left in labels
            for right in labels
        }

    def find(self, value: tuple[int, int]) -> tuple[int, int]:
        root = value
        while self.parent[root] != root:
            root = self.parent[root]
        while self.parent[value] != value:
            successor = self.parent[value]
            self.parent[value] = root
            value = successor
        return root

    def union(self, left: tuple[int, int], right: tuple[int, int]) -> None:
        left_root = self.find(left)
        right_root = self.find(right)
        if left_root != right_root:
            self.parent[right_root] = left_root

    def equivalent(self, left: tuple[int, int], right: tuple[int, int]) -> bool:
        return self.find(left) == self.find(right)


def subject_closure() -> EdgeClosure:
    closure = EdgeClosure(LABELS)
    for center, support in SUBJECT_ROWS:
        first = edge(center, support[0])
        for point in support[1:]:
            closure.union(first, edge(center, point))
    return closure


def effective_equalities() -> list[dict[str, object]]:
    records: list[dict[str, object]] = []
    for center, support in SUBJECT_ROWS:
        pivot = support[0]
        records.extend(
            {
                "center": center,
                "pivot": pivot,
                "other": point,
            }
            for point in support[1:]
        )
    return records


def assignment_matches(
    closure: EdgeClosure, family: Family, values: Sequence[int]
) -> bool:
    assignment = dict(zip(family.roles, values))
    if any(assignment[left] == assignment[right] for left, right in family.inequalities):
        return False
    return all(
        closure.equivalent(
            edge(assignment[a], assignment[b]),
            edge(assignment[c], assignment[d]),
        )
        for a, b, c, d in family.requirements
    )


def audit_family(
    closure: EdgeClosure,
    family: Family,
    labels: Sequence[int] = LABELS,
) -> dict[str, object]:
    inequality_count = 0
    matches: list[dict[str, int]] = []
    for values in itertools.product(labels, repeat=len(family.roles)):
        assignment = dict(zip(family.roles, values))
        if any(
            assignment[left] == assignment[right]
            for left, right in family.inequalities
        ):
            continue
        inequality_count += 1
        if assignment_matches(closure, family, values):
            matches.append(assignment)
    return {
        "family": family.name,
        "tier": family.tier,
        "source": family.source,
        "roles": list(family.roles),
        "declared_inequalities": [list(pair) for pair in family.inequalities],
        "required_edge_closures": [list(item) for item in family.requirements],
        "total_role_assignments": len(labels) ** len(family.roles),
        "assignments_satisfying_declared_inequalities": inequality_count,
        "match_count": len(matches),
        "matches": matches,
    }


def verify_sources() -> None:
    for relative, expected in SOURCE_HASHES.items():
        actual = sha256_file(ROOT / relative)
        if actual != expected:
            raise AssertionError(f"Lean bank source hash drift: {relative}")
    certificate = (
        ROOT
        / "scratch/atail-force/second_center_metric_certificate/"
        "certificate_normalized_cegar_456_5row.json"
    )
    if sha256_file(certificate) != SUBJECT_CERTIFICATE_SHA256:
        raise AssertionError("normalized five-row certificate hash drift")
    payload = json.loads(certificate.read_text(encoding="utf-8"))
    if payload["row_set_sha256"] != SUBJECT_FULL_ROW_SET_SHA256:
        raise AssertionError("normalized five-row row-set provenance drift")
    if payload["generator_sha256"] != SUBJECT_GENERATOR_SHA256:
        raise AssertionError("normalized five-row generator provenance drift")
    if sum(str(value).strip() != "0" for value in payload["multipliers"]) != 11:
        raise AssertionError("normalized five-row effective equality count drift")


