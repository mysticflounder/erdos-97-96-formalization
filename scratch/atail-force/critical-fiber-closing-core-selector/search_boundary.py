#!/usr/bin/env python3
"""Search a finite retained-radius matching boundary.

This is theorem-discovery only.  It searches row hypergraphs satisfying the
exact two-circle intersection gate, then asks Z3 for a cyclic boundary order
satisfying every shared-pair alternation constraint generated by those rows.
"""

from __future__ import annotations

import itertools
import random

import z3


N = 12
FIRST_APEX = 4
RETAINED = frozenset({0, 3, 5, 6, 7})
BLOCKER = (1, 0, 0, 0, 0, 8, 7, 6, 5, 2, 2, 3)
REQUIRED = tuple(
    frozenset(source for source in range(N) if BLOCKER[source] == center)
    for center in range(N)
)


def candidates(center: int) -> list[frozenset[int]]:
    if center == FIRST_APEX:
        return [frozenset(s) for s in itertools.combinations(RETAINED, 4)]
    out: list[frozenset[int]] = []
    for row in itertools.combinations((x for x in range(N) if x != center), 4):
        support = frozenset(row)
        if not REQUIRED[center] <= support:
            continue
        if len(support & RETAINED) > 2:
            continue
        out.append(support)
    return out


def strongly_connected(rows: tuple[frozenset[int], ...]) -> bool:
    for start in range(N):
        seen = {start}
        frontier = [start]
        while frontier:
            center = frontier.pop()
            for target in rows[center]:
                if target not in seen:
                    seen.add(target)
                    frontier.append(target)
        if len(seen) != N:
            return False
    return True


def between(x: z3.ArithRef, y: z3.ArithRef, z: z3.ArithRef) -> z3.BoolRef:
    return z3.Or(z3.And(x < z, z < y), z3.And(y < z, z < x))


def cyclic_model(rows: tuple[frozenset[int], ...]) -> list[int] | None:
    pos = [z3.Int(f"p_{i}") for i in range(N)]
    solver = z3.Solver()
    solver.add(*(z3.And(0 <= p, p < N) for p in pos))
    solver.add(z3.Distinct(pos))
    solver.add(pos[0] == 0)

    classes = list(rows)
    classes[FIRST_APEX] = RETAINED
    for first_center in range(N):
        for second_center in range(first_center + 1, N):
            shared = sorted(classes[first_center] & classes[second_center])
            for first_point, second_point in itertools.combinations(shared, 2):
                if second_point in {first_center, second_center}:
                    first_point, second_point = second_point, first_point
                if second_point in {first_center, second_center}:
                    continue
                solver.add(
                    between(pos[first_center], pos[second_center], pos[first_point])
                    != between(pos[first_center], pos[second_center], pos[second_point])
                )
    if solver.check() != z3.sat:
        return None
    model = solver.model()
    return sorted(range(N), key=lambda x: model.eval(pos[x]).as_long())


def search(seed: int = 9796, attempts: int = 50_000) -> None:
    rng = random.Random(seed)
    pools = [candidates(center) for center in range(N)]
    order = sorted(range(N), key=lambda center: len(pools[center]))

    for attempt in range(attempts):
        if attempt % 1000 == 0:
            print(f"checked={attempt}", flush=True)
        rows: list[frozenset[int] | None] = [None] * N
        ok = True
        for center in order:
            choices = pools[center][:]
            rng.shuffle(choices)
            chosen = next(
                (
                    row
                    for row in choices
                    if all(
                        other is None
                        or other == row
                        or len(other & row) <= 2
                        for other in rows
                    )
                ),
                None,
            )
            if chosen is None:
                ok = False
                break
            rows[center] = chosen
        if not ok:
            continue
        result = tuple(row for row in rows if row is not None)
        if len(result) != N or not strongly_connected(result):
            continue
        boundary = cyclic_model(result)
        if boundary is None:
            continue
        print(f"attempt={attempt}")
        print("rows=[")
        for row in result:
            print(f"  {sorted(row)},")
        print("]")
        print(f"boundary={boundary}")
        return
    raise SystemExit("no witness found")


if __name__ == "__main__":
    search()
