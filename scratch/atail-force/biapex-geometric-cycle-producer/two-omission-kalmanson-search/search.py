#!/usr/bin/env python3
"""Bounded two-omission search with equality-quotiented ordinal closure.

This is a finite structural theorem-discovery experiment, not a Lean proof
and not a model of the live robust branch.  Its inherited profile is
``(8,4,5)`` and its physical second opposite cap has cardinality four; the
live large-cap robust endpoint carries a lower bound of six.  No production
theorem extracts this fixed profile from ``FrontierBiApexRobustResidual``.

The script replays the current robust producer-bank checkpoint, imposes
omission of both physical apices from the critical blocker map, and eagerly
excludes the production two-row shared-late-pair Kalmanson terminal.  Every
decoded model is independently checked against the original structural
verifier and the complete equality-quotiented graph generated by the two
constructors of ``SelectedRowOrdinalComparison`` before it can be reported
as a theorem-discovery survivor.

Generic ordinal cycles become bounded CEGAR cuts with explicit row/equality
witnesses.  Current producer-bank matches are also rescanned and cut before a
survivor is accepted.  UNKNOWN and every budget stop fail closed.
"""

