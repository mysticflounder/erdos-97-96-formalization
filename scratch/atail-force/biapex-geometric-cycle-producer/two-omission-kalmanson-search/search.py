DistanceTerm = tuple[int, int]


@dataclass(frozen=True)
class EqualityEdge:
    left: DistanceTerm
    right: DistanceTerm
    center: int | None
    source: str


@dataclass(frozen=True)
class StrictEdge:
    left: DistanceTerm
    right: DistanceTerm
    center: int
    constructor: str
    quadruple: tuple[int, int, int, int]


@dataclass(frozen=True)
class LinearConstraint:
    name: str
    kind: str
    payload: tuple[int, ...]
    center: int | None
    formula: z3.BoolRef


def load_module(path: Path, name: str) -> Any:
    spec = importlib.util.spec_from_file_location(name, path)
    if spec is None or spec.loader is None:
        raise RuntimeError(f"cannot import {path}")
    module = importlib.util.module_from_spec(spec)
    spec.loader.exec_module(module)
    return module


def sha256(path: Path) -> str:
    return hashlib.sha256(path.read_bytes()).hexdigest()


def canonical_sha256(value: object) -> str:
    return hashlib.sha256(
        json.dumps(value, sort_keys=True, separators=(",", ":")).encode()
    ).hexdigest()


def atomic_write(path: Path, value: Mapping[str, object]) -> None:
    rendered = json.dumps(value, indent=2, sort_keys=True) + "\n"
    temporary = path.with_suffix(path.suffix + ".tmp")
    temporary.write_text(rendered, encoding="utf-8")
    temporary.replace(path)


def dist(left: int, right: int) -> DistanceTerm:
    if left == right:
        raise ValueError("distance term has identical endpoints")
    return tuple(sorted((left, right)))


def named_term(surface: Any, term: DistanceTerm) -> list[str]:
    return [surface.by_label[term[0]], surface.by_label[term[1]]]


def supports_by_center(rows: Iterable[Any]) -> dict[int, frozenset[int]]:
    rows = tuple(rows)
    answer = {row.center: frozenset(row.support) for row in rows}
    if len(answer) != len(rows):
        raise ValueError("represented rows repeat a center")
    return answer


def shared_late_pair_witness(
    order: Sequence[int], supports: Mapping[int, frozenset[int]]
) -> tuple[int, int, int, int] | None:
    """Return ``a<b<c<d`` with rows at ``a,b`` both containing ``c,d``."""

    for a, b, c, d in itertools.combinations(tuple(order), 4):
        if {c, d} <= supports[a] and {c, d} <= supports[b]:
            return a, b, c, d
    return None


def equality_edges(
    rows: Iterable[Any], ambient: Any
) -> tuple[EqualityEdge, ...]:
    answer: list[EqualityEdge] = []
    for row in rows:
        support = tuple(sorted(row.support))
        anchor = support[0]
        for point in support[1:]:
            answer.append(
                EqualityEdge(
                    dist(row.center, anchor),
                    dist(row.center, point),
                    row.center,
                    f"selected-row:{row.center}",
                )
            )
    ambient_support = tuple(sorted(ambient.support))
    ambient_anchor = ambient_support[0]
    for point in ambient_support[1:]:
        answer.append(
            EqualityEdge(
                dist(ambient.center, ambient_anchor),
                dist(ambient.center, point),
                None,
                "ambient-first-apex-class",
            )
        )
    return tuple(answer)


def equality_adjacency(
    edges: Iterable[EqualityEdge],
) -> dict[DistanceTerm, list[tuple[DistanceTerm, EqualityEdge]]]:
    adjacency: dict[DistanceTerm, list[tuple[DistanceTerm, EqualityEdge]]] = {}
    for edge in edges:
        adjacency.setdefault(edge.left, []).append((edge.right, edge))
        adjacency.setdefault(edge.right, []).append((edge.left, edge))
    return adjacency


def equality_components(
    terms: Iterable[DistanceTerm],
    adjacency: Mapping[DistanceTerm, Sequence[tuple[DistanceTerm, EqualityEdge]]],
) -> dict[DistanceTerm, int]:
    component: dict[DistanceTerm, int] = {}
    for start in sorted(set(terms) | set(adjacency)):
        if start in component:
            continue
        identifier = len(set(component.values()))
        component[start] = identifier
        queue = deque([start])
        while queue:
            current = queue.popleft()
            for neighbor, _edge in adjacency.get(current, ()):
                if neighbor in component:
                    continue
                component[neighbor] = identifier
                queue.append(neighbor)
    return component


def equality_path(
    start: DistanceTerm,
    target: DistanceTerm,
    adjacency: Mapping[DistanceTerm, Sequence[tuple[DistanceTerm, EqualityEdge]]],
) -> tuple[EqualityEdge, ...]:
    if start == target:
        return ()
    parent: dict[DistanceTerm, tuple[DistanceTerm, EqualityEdge] | None] = {
        start: None
    }
    queue = deque([start])
    while queue and target not in parent:
        current = queue.popleft()
        for neighbor, edge in adjacency.get(current, ()):
            if neighbor in parent:
                continue
            parent[neighbor] = current, edge
            queue.append(neighbor)
    if target not in parent:
        raise RuntimeError(f"no equality path between {start} and {target}")
    path: list[EqualityEdge] = []
    current = target
    while current != start:
        predecessor = parent[current]
        if predecessor is None:
            raise RuntimeError("broken equality-path predecessor")
        current, edge = predecessor
        path.append(edge)
    path.reverse()
    return tuple(path)


def strict_edges(
    order: Sequence[int], supports: Mapping[int, frozenset[int]]
) -> tuple[StrictEdge, ...]:
    """Instantiate both production ``SelectedRowOrdinalComparison`` rules."""

    answer: list[StrictEdge] = []
    for a, b, c, d in itertools.combinations(tuple(order), 4):
        if {c, d} <= supports[a]:
            answer.append(
                StrictEdge(
                    dist(b, c),
                    dist(b, d),
                    a,
                    "diagonalEqSide",
                    (a, b, c, d),
                )
            )
        if {a, d} <= supports[c]:
            answer.append(
                StrictEdge(
                    dist(a, b),
                    dist(b, d),
                    c,
                    "diagonalEqLastSide",
                    (a, b, c, d),
                )
            )
    return tuple(answer)


def directed_cycle(
    edges: Sequence[StrictEdge], component: Mapping[DistanceTerm, int]
) -> tuple[StrictEdge, ...] | None:
    adjacency: dict[int, list[StrictEdge]] = {}
    for edge in edges:
        left = component[edge.left]
        right = component[edge.right]
        if left == right:
            return (edge,)
        adjacency.setdefault(left, []).append(edge)

    color: dict[int, int] = {}
    stack_nodes: list[int] = []
    stack_edges: list[StrictEdge] = []
    position: dict[int, int] = {}

    def visit(node: int) -> tuple[StrictEdge, ...] | None:
        color[node] = 1
        position[node] = len(stack_nodes)
        stack_nodes.append(node)
        for edge in adjacency.get(node, ()):
            target = component[edge.right]
            if color.get(target, 0) == 0:
                stack_edges.append(edge)
                found = visit(target)
                if found is not None:
                    return found
                stack_edges.pop()
            elif color[target] == 1:
                start = position[target]
                return tuple(stack_edges[start:] + [edge])
        stack_nodes.pop()
        position.pop(node)
        color[node] = 2
        return None

    for node in sorted(set(component.values())):
        if color.get(node, 0) != 0:
            continue
        found = visit(node)
        if found is not None:
            return found
    return None


def ordinal_cycle_certificate(
    surface: Any, rows: Iterable[Any]
) -> dict[str, object] | None:
    rows = tuple(rows)
    supports = supports_by_center(rows)
    order = tuple(surface.frame_order)
    ambient = surface.ambient_first_apex_row()
    equalities = equality_edges(rows, ambient)
    adjacency = equality_adjacency(equalities)
    strict = strict_edges(order, supports)
    all_terms = {
        term
        for edge in (*equalities, *strict)
        for term in (edge.left, edge.right)
    }
    components = equality_components(all_terms, adjacency)
    cycle = directed_cycle(strict, components)
    if cycle is None:
        return None

    connectors: list[tuple[EqualityEdge, ...]] = []
    dependency_centers = {edge.center for edge in cycle}
    for index, edge in enumerate(cycle):
        following = cycle[(index + 1) % len(cycle)]
        connector = equality_path(edge.right, following.left, adjacency)
        connectors.append(connector)
        dependency_centers.update(
            item.center for item in connector if item.center is not None
        )

    def serialize_equality(edge: EqualityEdge) -> dict[str, object]:
        return {
            "left": named_term(surface, edge.left),
            "right": named_term(surface, edge.right),
            "center": (
                surface.by_label[edge.center]
                if edge.center is not None
                else None
            ),
            "source": edge.source,
        }

    return {
        "lean_consumer": ORDINAL_CYCLE_CONSUMER,
        "cycle_length": len(cycle),
        "strict_edges": [
            {
                "left": named_term(surface, edge.left),
                "right": named_term(surface, edge.right),
                "center": surface.by_label[edge.center],
                "constructor": edge.constructor,
                "quadruple": [surface.by_label[x] for x in edge.quadruple],
            }
            for edge in cycle
        ],
        "equality_connectors": [
            [serialize_equality(edge) for edge in connector]
            for connector in connectors
        ],
        "dependency_center_ids": sorted(dependency_centers),
        "dependency_centers": [
            surface.by_label[center] for center in sorted(dependency_centers)
        ],
    }


def full_linear_kalmanson_certificate(
    surface: Any, rows: Iterable[Any], *, timeout_ms: int
) -> dict[str, object]:
    """Decide the full homogeneous strict Kalmanson distance system.

    Strict slacks are normalized to at least one.  This is equivalent to the
    finite homogeneous strict system: any feasible strict assignment can be
    scaled by the reciprocal of its smallest positive slack.
    """

    rows = tuple(rows)
    n = surface.frame.n
    variables = {
        (left, right): z3.Real(f"linear_d_{left}_{right}")
        for left in range(n)
        for right in range(left + 1, n)
    }

    def value(left: int, right: int) -> z3.ArithRef:
        if left == right:
            return z3.RealVal(0)
        return variables[dist(left, right)]

    constraints: list[LinearConstraint] = []
    for row in rows:
        support = tuple(sorted(row.support))
        anchor = support[0]
        for point in support[1:]:
            constraints.append(
                LinearConstraint(
                    f"row_{row.center}_{anchor}_{point}",
                    "selected_row_equality",
                    (row.center, anchor, point),
                    row.center,
                    value(row.center, anchor) == value(row.center, point),
                )
            )
    ambient = surface.ambient_first_apex_row()
    ambient_support = tuple(sorted(ambient.support))
    ambient_anchor = ambient_support[0]
    for point in ambient_support[1:]:
        constraints.append(
            LinearConstraint(
                f"ambient_{ambient.center}_{ambient_anchor}_{point}",
                "ambient_first_apex_equality",
                (ambient.center, ambient_anchor, point),
                None,
                value(ambient.center, ambient_anchor)
                == value(ambient.center, point),
            )
        )
    for a, b, c, d in itertools.combinations(tuple(surface.frame_order), 4):
        diagonal_sum = value(a, c) + value(b, d)
        constraints.append(
            LinearConstraint(
                f"kal1_{a}_{b}_{c}_{d}",
                "strict_kalmanson_ab_cd",
                (a, b, c, d),
                None,
                diagonal_sum >= value(a, b) + value(c, d) + 1,
            )
        )
        constraints.append(
            LinearConstraint(
                f"kal2_{a}_{b}_{c}_{d}",
                "strict_kalmanson_ad_bc",
                (a, b, c, d),
                None,
                diagonal_sum >= value(a, d) + value(b, c) + 1,
            )
        )

    solver = z3.Solver()
    solver.set(timeout=timeout_ms, random_seed=0)
    by_name = {item.name: item for item in constraints}
    for item in constraints:
        solver.assert_and_track(item.formula, z3.Bool(item.name))
    verdict = solver.check()
    if verdict == z3.unknown:
        return {"status": "UNKNOWN", "reason": solver.reason_unknown()}
    if verdict == z3.sat:
        return {
            "status": "SAT",
            "constraint_count": len(constraints),
            "normalized_strict_slack": 1,
        }

    core_names = [str(name) for name in solver.unsat_core()]
    core = [by_name[name] for name in core_names]
    deletion_minimal = True
    changed = True
    while changed:
        changed = False
        for item in tuple(core):
            candidate = [other for other in core if other != item]
            check = z3.Solver()
            check.set(timeout=timeout_ms, random_seed=0)
            check.add(*(other.formula for other in candidate))
            candidate_verdict = check.check()
            if candidate_verdict == z3.unsat:
                core = candidate
                changed = True
            elif candidate_verdict == z3.unknown:
                deletion_minimal = False

    dependency_centers = sorted(
        {item.center for item in core if item.center is not None}
    )
    return {
        "status": "UNSAT",
        "constraint_count": len(constraints),
        "initial_core_count": len(core_names),
        "core_count": len(core),
        "deletion_minimal": deletion_minimal,
        "normalized_strict_slack": 1,
        "dependency_center_ids": dependency_centers,
        "dependency_centers": [
            surface.by_label[center] for center in dependency_centers
        ],
        "core": [
            {
                "name": item.name,
                "kind": item.kind,
                "payload": [surface.by_label[x] for x in item.payload],
                "center": (
                    surface.by_label[item.center]
                    if item.center is not None
                    else None
                ),
            }
            for item in core
        ],
    }


def add_eager_shared_pair_constraints(surface: Any, solver: z3.Solver) -> int:
    count = 0
    for a, b, c, d in itertools.combinations(tuple(surface.frame_order), 4):
        solver.add(
            z3.Not(
                z3.And(
                    surface.member(a, c),
                    surface.member(a, d),
                    surface.member(b, c),
                    surface.member(b, d),
                )
            )
        )
        count += 1
    return count


def smoke_gates() -> dict[str, object]:
    fin12_rows = {
        center: frozenset(row)
        for center, row in enumerate(
            (
                (1, 2, 3, 4),
                (2, 3, 4, 11),
                (0, 3, 4, 5),
                (2, 4, 5, 6),
                (1, 6, 7, 8),
                (3, 6, 7, 8),
                (5, 7, 8, 9),
                (6, 8, 9, 10),
                (7, 9, 10, 11),
                (0, 8, 10, 11),
                (0, 1, 9, 11),
                (0, 1, 2, 10),
            )
        )
    }
    witness = shared_late_pair_witness(tuple(range(12)), fin12_rows)
    if witness != (0, 1, 2, 3):
        raise RuntimeError(f"Fin12 shared-pair smoke drift: {witness}")
    clean = {
        0: frozenset({1, 2}),
        1: frozenset({0, 3}),
        2: frozenset({0, 3}),
        3: frozenset({0, 1}),
    }
    if shared_late_pair_witness((0, 1, 2, 3), clean) is not None:
        raise RuntimeError("clean shared-pair smoke unexpectedly rejected")
    return {
        "fin12_regression_shared_late_pair": list(witness),
        "clean_negative_fixture": "PASS",
        "production_consumer": SHARED_PAIR_CONSUMER,
    }


def decoded_named_rows(
    surface: Any, rows: Iterable[Any], choices: Mapping[int, int]
) -> list[dict[str, object]]:
    return [
        {
            "center": surface.by_label[row.center],
            "support": sorted(surface.by_label[x] for x in row.support),
            "candidate_index": choices[row.center],
            "exact_from_blocker_image": row.exact,
        }
        for row in sorted(rows, key=lambda item: surface.by_label[item.center])
    ]


def decoded_named_blockers(
    surface: Any, blockers: Mapping[int, int]
) -> dict[str, str]:
    return {
        surface.by_label[source]: surface.by_label[center]
        for source, center in sorted(
            blockers.items(), key=lambda item: surface.by_label[item[0]]
        )
    }


def audit_stored_survivors(
    surface: Any, checkpoint: Mapping[str, object], first_apex: str,
    second_apex: str,
) -> list[dict[str, object]]:
    answer: list[dict[str, object]] = []
    for survivor in checkpoint["structural_survivors"]:
        blockers = survivor["blockers"]
        omitted = all(
            center not in {first_apex, second_apex}
            for center in blockers.values()
        )
        row_supports = {
            surface.by_name[row["center"]]: frozenset(
                surface.by_name[point] for point in row["support"]
            )
            for row in survivor["rows"]
        }
        shared = shared_late_pair_witness(surface.frame_order, row_supports)
        answer.append(
            {
                "iteration": survivor["iteration"],
                "satisfies_biapex_omission": omitted,
                "shared_late_pair": (
                    [surface.by_label[x] for x in shared]
                    if shared is not None
                    else None
                ),
            }
        )
    return answer


def main() -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--checkpoint", type=Path, default=CHECKPOINT)
    parser.add_argument("--output", type=Path, required=True)
    parser.add_argument("--resume-result", type=Path)
    parser.add_argument("--timeout-ms", type=int, default=30_000)
    parser.add_argument("--wall-seconds", type=float, default=120.0)
    parser.add_argument("--max-refinements", type=int, default=20_000)
    parser.add_argument("--marco-max-calls", type=int, default=64)
    parser.add_argument("--marco-max-cores", type=int, default=4)
    parser.add_argument("--linear-timeout-ms", type=int, default=5_000)
    parser.add_argument(
        "--allow-nonlive-profile",
        action="store_true",
        help=(
            "acknowledge that PROFILE=(8,4,5), with physical oppCap2 card 4, "
            "does not instantiate the live oppCap2>=6 robust branch"
        ),
    )
    args = parser.parse_args()
    if min(
        args.timeout_ms,
        args.wall_seconds,
        args.max_refinements,
        args.marco_max_calls,
        args.marco_max_cores,
        args.linear_timeout_ms,
    ) <= 0:
        raise SystemExit("all budgets must be positive")
    if not args.allow_nonlive_profile:
        raise SystemExit(
            "refusing to mine a non-live fixed profile without "
            "--allow-nonlive-profile"
        )

    bank = load_module(BANK_SCRIPT, "p97_biapex_kalmanson_current_bank")
    checkpoint = json.loads(args.checkpoint.read_text(encoding="utf-8"))
    surface = bank.robust.Surface()
    surface.frame_order = tuple(bank.shadow.hull_order(surface.frame))
    if tuple(surface.by_label[x] for x in surface.frame_order) != tuple(
        bank.robust.HULL_NAMES
    ):
        raise RuntimeError("hull-order role mapping drift")

    smokes = smoke_gates()
    solver = surface.base_solver()
    solver.set(timeout=args.timeout_ms, random_seed=0)
    for cut in checkpoint["bank_cuts"]:
        solver.add(bank.clause_for_bank_cut(surface, cut))

    first_apex = bank.robust.FIRST_APEX
    second_apex = bank.robust.SECOND_APEX
    first_apex_label = surface.by_name[first_apex]
    second_apex_label = surface.by_name[second_apex]
    for variable in surface.blocker.values():
        solver.add(variable != first_apex_label, variable != second_apex_label)
    eager_count = add_eager_shared_pair_constraints(surface, solver)

    source_hash_drift = checkpoint.get("source_sha256") != bank.source_hashes()
    stored_audit = audit_stored_survivors(
        surface, checkpoint, first_apex, second_apex
    )
    prior_run: dict[str, object] | None = None
    if args.resume_result is not None:
        prior = json.loads(args.resume_result.read_text(encoding="utf-8"))
        if prior.get("schema") != (
            "p97-atail-biapex-two-omission-kalmanson-search-v1"
        ):
            raise RuntimeError("resume result schema mismatch")
        if prior.get("inputs", {}).get(str(args.checkpoint.relative_to(ROOT))) != (
            sha256(args.checkpoint)
        ):
            raise RuntimeError("resume result checkpoint drift")
        structural_refinements = list(prior["structural_refinements"])
        ordinal_refinements = list(prior["ordinal_refinements"])
        linear_kalmanson_refinements = list(
            prior["linear_kalmanson_refinements"]
        )
        current_bank_cuts = list(prior["current_bank_cuts"])
        for refinement in structural_refinements:
            solver.add(
                bank.clause_for_structural_refinement(surface, refinement)
            )
        for refinement in (*ordinal_refinements, *linear_kalmanson_refinements):
            row_pattern = {
                int(center): int(index)
                for center, index in refinement["row_pattern"].items()
            }
            solver.add(
                z3.Or(
                    *(
                        surface.choice[center] != index
                        for center, index in row_pattern.items()
                    )
                )
            )
        for cut in current_bank_cuts:
            solver.add(bank.clause_for_bank_cut(surface, cut))
        prior_run = {
            "path": str(args.resume_result),
            "sha256": sha256(args.resume_result),
            "status": prior.get("status"),
            "elapsed_seconds": prior.get("elapsed_seconds"),
            "structural_refinement_count": len(structural_refinements),
            "ordinal_refinement_count": len(ordinal_refinements),
            "linear_kalmanson_refinement_count": len(
                linear_kalmanson_refinements
            ),
            "current_bank_cut_count": len(current_bank_cuts),
        }
    else:
        structural_refinements = []
        ordinal_refinements = []
        linear_kalmanson_refinements = []
        current_bank_cuts = []
    verified_survivor: dict[str, object] | None = None
    verdict: z3.CheckSatResult | None = None
    reason_unknown: str | None = None
    started = time.monotonic()

    for attempt in range(1, args.max_refinements + 1):
        remaining = args.wall_seconds - (time.monotonic() - started)
        if remaining <= 0:
            reason_unknown = "wall budget"
            break
        solver.set(timeout=min(args.timeout_ms, max(1, int(remaining * 1000))))
        verdict = solver.check()
        if verdict != z3.sat:
            if verdict == z3.unknown:
                reason_unknown = solver.reason_unknown()
            break

        model = solver.model()
        blockers = surface.blockers_from_model(model)
        rows = surface.rows_with_blocker_exactness(
            surface.rows_from_model(model), blockers
        )
        choices = surface.row_choice_indices(model)
        ok, reason = surface.verify_model(rows, blockers)
        if not ok:
            if reason is None:
                raise RuntimeError("structural verifier rejected without reason")
            refinement = bank.structural_refinement(surface, reason, choices)
            structural_refinements.append(refinement)
            solver.add(bank.clause_for_structural_refinement(surface, refinement))
            continue
        if any(
            center in {first_apex_label, second_apex_label}
            for center in blockers.values()
        ):
            raise RuntimeError("decoded model violated a physical-apex omission")

        supports = supports_by_center(rows)
        shared = shared_late_pair_witness(surface.frame_order, supports)
        if shared is not None:
            raise RuntimeError(
                "decoded model violated an eager shared-late-pair constraint: "
                f"{shared}"
            )

        cycle = ordinal_cycle_certificate(surface, rows)
        if cycle is not None:
            centers = [int(x) for x in cycle["dependency_center_ids"]]
            if not centers:
                raise RuntimeError("ordinal cycle has no selected-row dependency")
            record = {
                "attempt": attempt,
                "row_pattern": {str(center): choices[center] for center in centers},
                "certificate": cycle,
            }
            clause = z3.Or(
                *(surface.choice[center] != choices[center] for center in centers)
            )
            if not z3.is_true(model.eval(z3.Not(clause), model_completion=True)):
                raise RuntimeError("ordinal cut does not exclude current witness")
            ordinal_refinements.append(record)
            solver.add(clause)
            continue

        linear = full_linear_kalmanson_certificate(
            surface, rows, timeout_ms=args.linear_timeout_ms
        )
        if linear["status"] == "UNKNOWN":
            reason_unknown = (
                "full linear Kalmanson verifier returned UNKNOWN: "
                f"{linear.get('reason')}"
            )
            break
        if linear["status"] == "UNSAT":
            centers = [int(x) for x in linear["dependency_center_ids"]]
            if not centers:
                reason_unknown = (
                    "full Kalmanson system was UNSAT without a selected-row "
                    "dependency; refusing an unscoped cut"
                )
                break
            record = {
                "attempt": attempt,
                "row_pattern": {str(center): choices[center] for center in centers},
                "certificate": linear,
            }
            clause = z3.Or(
                *(surface.choice[center] != choices[center] for center in centers)
            )
            if not z3.is_true(model.eval(z3.Not(clause), model_completion=True)):
                raise RuntimeError("linear Kalmanson cut does not exclude witness")
            linear_kalmanson_refinements.append(record)
            solver.add(clause)
            continue
        if linear["status"] != "SAT":
            raise RuntimeError(f"unexpected linear Kalmanson status: {linear}")

        selected_matches = bank.scan(surface, rows, exact_centers=frozenset())
        exact_centers = frozenset(blockers.values())
        exact_matches = bank.scan(surface, rows, exact_centers=exact_centers)
        if selected_matches or exact_matches:
            marco = bank.marco_row_cores(
                surface,
                tuple(rows),
                exact_centers=exact_centers,
                max_calls=args.marco_max_calls,
                max_cores=args.marco_max_cores,
            )
            before = len(current_bank_cuts)
            state = {"bank_cuts": current_bank_cuts}
            added = bank.add_core_cuts(
                surface,
                solver,
                state,
                tuple(rows),
                choices,
                marco,
                exact_centers=exact_centers,
                iteration=attempt,
            )
            if added <= 0 or len(current_bank_cuts) == before:
                reason_unknown = (
                    "current bank recognized the witness but bounded MARCO "
                    f"produced no independently rescanned core: {marco['status']}"
                )
                break
            continue

        # Independent acceptance replay: no survivor is serialized before all
        # original and added verifier layers have been rerun on the decoded rows.
        ok, reason = surface.verify_model(rows, blockers)
        if not ok:
            raise RuntimeError(f"acceptance replay failed structural check: {reason}")
        if shared_late_pair_witness(surface.frame_order, supports_by_center(rows)):
            raise RuntimeError("acceptance replay found shared-late-pair terminal")
        if ordinal_cycle_certificate(surface, rows) is not None:
            raise RuntimeError("acceptance replay found ordinal cycle")
        linear_replay = full_linear_kalmanson_certificate(
            surface, rows, timeout_ms=args.linear_timeout_ms
        )
        if linear_replay["status"] != "SAT":
            if linear_replay["status"] == "UNKNOWN":
                reason_unknown = (
                    "acceptance full linear Kalmanson replay returned UNKNOWN: "
                    f"{linear_replay.get('reason')}"
                )
                break
            raise RuntimeError("acceptance replay found linear Kalmanson UNSAT")
        if bank.scan(surface, rows, exact_centers=exact_centers):
            raise RuntimeError("acceptance replay found a current-bank match")
        serialized_rows = decoded_named_rows(surface, rows, choices)
        serialized_blockers = decoded_named_blockers(surface, blockers)
        verified_survivor = {
            "attempt": attempt,
            "rows": serialized_rows,
            "blockers": serialized_blockers,
            "row_assignment_sha256": canonical_sha256(serialized_rows),
            "blocker_assignment_sha256": canonical_sha256(serialized_blockers),
            "independent_structural_verifier": "PASS",
            "shared_late_pair_verifier": "PASS",
            "equality_quotiented_ordinal_cycle_verifier": "PASS",
            "full_linear_strict_kalmanson_verifier": "PASS",
            "current_producer_bank_rescan": "PASS",
        }
        break
    else:
        reason_unknown = "refinement budget"

    elapsed = time.monotonic() - started
    if verified_survivor is not None:
        status = "SAT_VERIFIED_SURVIVOR"
    elif verdict == z3.unsat:
        status = "UNSAT_BOUNDED_Z3_MAP_UNCHECKED_SOLVER_PROOF"
    else:
        status = "UNKNOWN_FAIL_CLOSED"

    result: dict[str, object] = {
        "schema": "p97-atail-biapex-two-omission-kalmanson-search-v1",
        "epistemic_status": (
            "BOUNDED_FINITE_STRUCTURAL_Z3_EXPERIMENT_NOT_LEAN_COVERAGE_PROOF"
        ),
        "status": status,
        "inputs": {
            str(BANK_SCRIPT.relative_to(ROOT)): sha256(BANK_SCRIPT),
            str(args.checkpoint.relative_to(ROOT)): sha256(args.checkpoint),
        },
        "checkpoint_source_hash_drift": source_hash_drift,
        "prior_run": prior_run,
        "scope_gate": {
            "classification": "THEOREM_DISCOVERY_ONLY_NONLIVE_PROFILE",
            "profile": list(bank.robust.PROFILE),
            "physical_oppCap2_card": len(bank.robust.OPP_CAP_2),
            "live_required_physical_oppCap2_card_lower_bound": 6,
            "live_extraction_theorem": None,
        },
        "stored_survivor_count": len(stored_audit),
        "stored_biapex_omission_count": sum(
            bool(item["satisfies_biapex_omission"]) for item in stored_audit
        ),
        "stored_survivor_audit": stored_audit,
        "smoke_gates": smokes,
        "hard_constraints": {
            "replayed_bank_cuts": len(checkpoint["bank_cuts"]),
            "omitted_blocker_centers": [first_apex, second_apex],
            "eager_shared_late_pair_quadruples": eager_count,
            "shared_late_pair_consumer": SHARED_PAIR_CONSUMER,
        },
        "budgets": {
            "timeout_ms": args.timeout_ms,
            "wall_seconds": args.wall_seconds,
            "max_refinements": args.max_refinements,
            "marco_max_calls": args.marco_max_calls,
            "marco_max_cores": args.marco_max_cores,
            "linear_timeout_ms": args.linear_timeout_ms,
        },
        "elapsed_seconds": elapsed,
        "structural_refinement_count": len(structural_refinements),
        "structural_refinements": structural_refinements,
        "ordinal_refinement_count": len(ordinal_refinements),
        "ordinal_refinements": ordinal_refinements,
        "linear_kalmanson_refinement_count": len(
            linear_kalmanson_refinements
        ),
        "linear_kalmanson_refinements": linear_kalmanson_refinements,
        "current_bank_cut_count": len(current_bank_cuts),
        "current_bank_cuts": current_bank_cuts,
    }
    if reason_unknown is not None:
        result["reason_unknown"] = reason_unknown
    if verified_survivor is not None:
        result["verified_survivor"] = verified_survivor

    atomic_write(args.output, result)
    print(
        json.dumps(
            {
                "status": status,
                "elapsed_seconds": elapsed,
                "stored_biapex_omission_count": result[
                    "stored_biapex_omission_count"
                ],
                "structural_refinement_count": len(structural_refinements),
                "ordinal_refinement_count": len(ordinal_refinements),
                "linear_kalmanson_refinement_count": len(
                    linear_kalmanson_refinements
                ),
                "current_bank_cut_count": len(current_bank_cuts),
            },
            sort_keys=True,
        )
    )
    return 0


