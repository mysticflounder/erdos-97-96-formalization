#!/usr/bin/env python3
"""Generate packed-source assignments and flat ingress for one all-one core.

This layer does not prove the geometric source assertions.  It exposes their
exact packed Boolean forms and bridges them, chunk by chunk, into the verified
``BVLogicalExpr`` generated by ``generate_all_one_bvexpr.py``.  The emitted
obligations JSON is the stable input for the semantic producer generator.
"""

from __future__ import annotations

import argparse
import hashlib
import json
from pathlib import Path

import z3

from all_one_source_terms import (
    atom,
    emit_bool,
    load_core,
    natural_key,
    source_contract,
)


PB_KINDS = {
    z3.Z3_OP_PB_AT_LEAST,
    z3.Z3_OP_PB_AT_MOST,
    z3.Z3_OP_PB_EQ,
}


def sha256(path: Path) -> str:
    return hashlib.sha256(path.read_bytes()).hexdigest()


def contains_pseudo_boolean(expression: z3.ExprRef) -> bool:
    return expression.decl().kind() in PB_KINDS or any(
        contains_pseudo_boolean(child) for child in expression.children()
    )


def requires_bv_decide(expression: z3.ExprRef) -> bool:
    """Whether the source/verified encodings are equivalent but not reducible."""
    kind = expression.decl().kind()
    return (
        kind in PB_KINDS
        or any(requires_bv_decide(child) for child in expression.children())
    )


def slot_expression(name: str, sort: str) -> str:
    value = atom(name)
    if sort == "Bool":
        return f"⟨BitVec.ofBool ({value})⟩"
    if sort in {"Int-as-unsigned-4", "BitVec(7)"}:
        return f"⟨{value}⟩"
    raise ValueError(f"unsupported mapped sort {sort!r} for {name}")


def rarray_expression(entries: list[str], start: int = 0, indent: int = 2) -> str:
    """Render a balanced Lean.RArray with absolute branch pivots."""
    if not entries:
        raise ValueError("cannot construct an empty source assignment")
    if len(entries) == 1:
        return f".leaf {entries[0]}"
    split = len(entries) // 2
    pivot = start + split
    left = rarray_expression(entries[:split], start, indent + 2)
    right = rarray_expression(entries[split:], pivot, indent + 2)
    pad = " " * indent
    return f".branch {pivot}\n{pad}({left})\n{pad}({right})"


def main() -> None:
    parser = argparse.ArgumentParser()
    parser.add_argument("--orbit", choices=("left", "middle", "right"), required=True)
    parser.add_argument("--core", type=Path, required=True)
    parser.add_argument("--map", type=Path, required=True)
    parser.add_argument("--certificate-module", required=True)
    parser.add_argument("--output-lean", type=Path, required=True)
    parser.add_argument("--output-obligations", type=Path, required=True)
    parser.add_argument("--chunk-size", type=int, default=64)
    parser.add_argument("--limit-chunks", type=int)
    args = parser.parse_args()

    short, spec, payload, assertions, counts = load_core(args.core, args.orbit)
    mapping = json.loads(args.map.read_text(encoding="utf-8"))
    if mapping.get("orbit") != spec["source_orbit"]:
        raise ValueError("core/map orbit mismatch")
    if mapping.get("canonical_qw", False):
        raise ValueError("canonical_qw maps cannot feed the source-faithful ingress")
    if int(mapping["selected_source_assertions"]) != len(assertions):
        raise ValueError("core/map source assertion count mismatch")
    if mapping.get("source_core_sha256") != sha256(args.core):
        raise ValueError("core/map source hash mismatch")
    if mapping.get("source_contract") != source_contract(spec):
        raise ValueError("core/map source semantic contract mismatch")
    if int(mapping["chunk_size"]) != args.chunk_size:
        raise ValueError("--chunk-size must match the BVLogicalExpr map")

    source_atoms: set[str] = set()

    def collect(expression: z3.ExprRef) -> None:
        if z3.is_const(expression) and expression.decl().kind() == z3.Z3_OP_UNINTERPRETED:
            source_atoms.add(str(expression))
        for child in expression.children():
            collect(child)

    for assertion in assertions:
        collect(assertion)
    if sorted(mapping["variables"], key=natural_key) != sorted(source_atoms, key=natural_key):
        raise ValueError("variable map names/order do not match the selected source AST")

    variables = sorted(
        mapping["variables"].items(), key=lambda item: int(item[1]["index"])
    )
    expected_indices = list(range(len(variables)))
    actual_indices = [int(info["index"]) for _name, info in variables]
    if actual_indices != expected_indices:
        raise ValueError("variable map is not a dense zero-based range")

    packed_terms = [emit_bool(assertion) for assertion in assertions]
    slot_cases = "\n".join(
        f"  | {info['index']} => {slot_expression(name, str(info['sort']))}"
        for name, info in variables
    ) + "\n  | _ => ⟨0#1⟩"
    assignment_tree = rarray_expression(
        [slot_expression(name, str(info["sort"])) for name, info in variables]
    )

    get_lemmas: list[str] = []
    eval_lemmas: list[str] = []
    for name, info in variables:
        index = int(info["index"])
        width = int(info["width"])
        slot = slot_expression(name, str(info["sort"]))
        get_lemmas.append(f"""@[simp] theorem sourceAssignment_get_{index:03d}
    (rows : RowBits) (blockers : BlockerBits) (shell : ShellBits)
    (q w : RoleBits) (b1 : FirstRowBits) (ranks : RankBits) :
    (sourceAssignment rows blockers shell q w b1 ranks).get {index} = {slot} := by
  unfold BVExpr.Assignment.get
  rw [Lean.RArray.get_eq_getImpl]
  rfl
""")
        expected = atom(name)
        if info["sort"] == "Bool":
            expected = f"BitVec.ofBool ({expected})"
        eval_lemmas.append(f"""@[simp 1100] theorem sourceAssignment_eval_var_{index:03d}
    (rows : RowBits) (blockers : BlockerBits) (shell : ShellBits)
    (q w : RoleBits) (b1 : FirstRowBits) (ranks : RankBits) :
    BVExpr.eval (sourceAssignment rows blockers shell q w b1 ranks)
      ((.var {index}) : BVExpr {width}) = {expected} := by
  rw [BVExpr.eval_var, sourceAssignment_get_{index:03d}]
  simp
""")

    all_chunks = [
        packed_terms[start : start + args.chunk_size]
        for start in range(0, len(packed_terms), args.chunk_size)
    ]
    chunks = all_chunks
    if args.limit_chunks is not None:
        if args.limit_chunks <= 0:
            raise ValueError("--limit-chunks must be positive")
        chunks = chunks[: args.limit_chunks]

    bridge_theorems: list[str] = []
    obligations: list[dict[str, object]] = []
    for chunk_number, terms in enumerate(chunks):
        start = chunk_number * args.chunk_size
        hypotheses = "\n".join(
            f"    (h{start + offset:04d} : ({term}) = true)"
            for offset, term in enumerate(terms)
        )
        alternatives = " | ".join("rfl" for _ in terms)
        cases: list[str] = []
        for offset, term in enumerate(terms):
            core_position = start + offset
            hypothesis = f"h{core_position:04d}"
            simplify = (
                "bvar, bnot, band, bor, bbeq, bimplies, bveq, bvult, bvule, "
                "bands, bors, atLeast, atMost, exactly, Gate.eval, and_assoc, or_assoc"
                ", BitVec.ule_eq_not_ult"
            )
            if requires_bv_decide(assertions[core_position]):
                cases.append(
                    f"  · simp [{simplify}] at {hypothesis} ⊢\n"
                    "    bv_decide"
                )
            else:
                cases.append(f"  · simpa [{simplify}] using {hypothesis}")
            source_item = payload["core"][core_position]
            obligations.append({
                "core_position": core_position,
                "source_index": int(source_item["index"]),
                "chunk": chunk_number,
                "chunk_offset": offset,
                "source_assertion": source_item["assertion"],
                "packed_term": term,
                "contains_pseudo_boolean": contains_pseudo_boolean(
                    assertions[core_position]
                ),
                "requires_bv_decide": requires_bv_decide(
                    assertions[core_position]
                ),
            })
        bridge_theorems.append(f"""theorem sourceChunk{chunk_number:03d}_each_of_flat
    (rows : RowBits) (blockers : BlockerBits) (shell : ShellBits)
    (q w : RoleBits) (b1 : FirstRowBits) (ranks : RankBits)
{hypotheses} :
    ∀ item ∈ sourceChunk{chunk_number:03d}Assertions,
      item.eval (sourceAssignment rows blockers shell q w b1 ranks) = true := by
  intro item hitem
  simp only [sourceChunk{chunk_number:03d}Assertions, List.mem_cons,
    List.not_mem_nil, or_false] at hitem
  rcases hitem with ({alternatives})
{"\n".join(cases)}
""")

    aggregate = ""
    complete = len(chunks) == len(all_chunks)
    if complete and mapping.get("has_certificate_theorem", False):
        endpoint_hypotheses = "\n".join(
            f"    (c{i:03d} : ∀ item ∈ sourceChunk{i:03d}Assertions,\n"
            f"      item.eval source = true)"
            for i in range(len(chunks))
        )
        endpoint_facts = "\n".join(
            f"  have hs{i:03d} := sourceChunk{i:03d}_sat_of_each source c{i:03d}"
            for i in range(len(chunks))
        )
        sat_arguments = " ".join(f"hs{i:03d}" for i in range(len(chunks)))
        flat_hypotheses = "\n".join(
            f"    (h{i:04d} : ({term}) = true)"
            for i, term in enumerate(packed_terms)
        )
        bridge_calls: list[str] = []
        for chunk_number, terms in enumerate(chunks):
            start = chunk_number * args.chunk_size
            arguments = " ".join(
                f"h{start + offset:04d}" for offset in range(len(terms))
            )
            bridge_calls.append(f"""    (sourceChunk{chunk_number:03d}_each_of_flat
      rows blockers shell q w b1 ranks {arguments})""")
        aggregate = f"""
/-- Complete itemwise chunk facts contradict the verified LRAT endpoint. -/
theorem false_of_sourceChunks_each (source : BVExpr.Assignment)
{endpoint_hypotheses} : False := by
{endpoint_facts}
  have hs := sourceExpr_sat_of_chunks source {sat_arguments}
  have hu := sourceExpr_unsat source
  exact Bool.noConfusion (hu.symm.trans hs)

/-- The exact packed source-core assertions contradict the verified endpoint. -/
theorem false_of_source_flat
    (rows : RowBits) (blockers : BlockerBits) (shell : ShellBits)
    (q w : RoleBits) (b1 : FirstRowBits) (ranks : RankBits)
{flat_hypotheses} : False := by
  exact false_of_sourceChunks_each
    (sourceAssignment rows blockers shell q w b1 ranks)
{"\n".join(bridge_calls)}

#print axioms false_of_sourceChunks_each
#print axioms false_of_source_flat
"""

    namespace = mapping["lean_namespace"]
    bridge_axiom_audit = (
        "#print axioms sourceChunk000_each_of_flat" if bridge_theorems else ""
    )
    lean = f"""/-
Generated packed-source ingress for `{spec['source_orbit']}`.

The row roles are fixed in the original boundary frame: hub {spec['hub']},
g0 spoke {spec['g0_spoke']}, and g1 spoke {spec['g1_spoke']}.  The `m` row
family is reserved for the source table carrying the actual fixed-H supports.
-/
import {args.certificate_module}
import Lean.Data.RArray

namespace {namespace}

open Std.Tactic.BVDecide

set_option maxRecDepth 1000000
set_option maxHeartbeats 0
set_option linter.unusedVariables false
set_option linter.unusedSimpArgs false

abbrev RowBits := BitVec 507
abbrev BlockerBits := BitVec 52
abbrev ShellBits := BitVec 13
abbrev RoleBits := BitVec 4
abbrev FirstRowBits := BitVec 13
abbrev RankBits := BitVec 546

abbrev rowBit (rows : RowBits) (family center point : Nat) : Bool :=
  rows.getLsbD (169 * family + 13 * center + point)

abbrev blockerAt (blockers : BlockerBits) (source : Nat) : BitVec 4 :=
  blockers.extractLsb' (4 * source) 4

abbrev shellBit (shell : ShellBits) (point : Nat) : Bool :=
  shell.getLsbD point

abbrev firstRowBit (row : FirstRowBits) (point : Nat) : Bool :=
  row.getLsbD point

abbrev rankAtom (ranks : RankBits) (index : Nat) : BitVec 7 :=
  ranks.extractLsb' (7 * index) 7

/-- Exact dense slot function recorded by the generated variable map. -/
def sourceSlot
    (rows : RowBits) (blockers : BlockerBits) (shell : ShellBits)
    (q w : RoleBits) (b1 : FirstRowBits) (ranks : RankBits) :
    Nat → BVExpr.PackedBitVec
{slot_cases}

/-- Exact packed assignment consumed by this orbit's BVLogicalExpr. -/
def sourceAssignment
    (rows : RowBits) (blockers : BlockerBits) (shell : ShellBits)
    (q w : RoleBits) (b1 : FirstRowBits) (ranks : RankBits) :
    BVExpr.Assignment :=
  {assignment_tree}

{"\n".join(get_lemmas)}

{"\n".join(eval_lemmas)}

{"\n".join(bridge_theorems)}

{bridge_axiom_audit}

{aggregate}

end {namespace}
"""

    args.output_lean.parent.mkdir(parents=True, exist_ok=True)
    args.output_obligations.parent.mkdir(parents=True, exist_ok=True)
    args.output_lean.write_text(lean, encoding="utf-8")
    obligation_payload = {
        "schema": "p97-exact5-card13-all-one-packed-source-obligations-v1",
        "orbit": spec["source_orbit"],
        "short_orbit": short,
        "source_counts": counts,
        "source_contract": source_contract(spec),
        "source_core": str(args.core),
        "variable_map": str(args.map),
        "certificate_module": args.certificate_module,
        "complete": complete,
        "has_certificate_endpoint": bool(mapping.get("has_certificate_theorem", False)),
        "obligations": obligations,
    }
    args.output_obligations.write_text(
        json.dumps(obligation_payload, indent=2, sort_keys=True) + "\n",
        encoding="utf-8",
    )
    print(json.dumps({
        "orbit": spec["source_orbit"],
        "assignment_entries": len(variables),
        "assertions": len(assertions),
        "bridges": len(chunks),
        "complete": complete,
        "certificate_endpoint": bool(mapping.get("has_certificate_theorem", False)),
    }, indent=2, sort_keys=True))


if __name__ == "__main__":
    main()
