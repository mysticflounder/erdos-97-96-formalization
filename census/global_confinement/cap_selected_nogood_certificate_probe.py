"""Mine compact row-choice certificates for the direct card-eleven probe.

The direct cap-selected classifier rejects monotone DFS prefixes by closing
the equalities generated by their selected rows and finding a formalized
metric core.  This probe records a proof-facing explanation for every such
rejection:

* exhaustively minimize the concrete center/support choices needed by the
  detected core;
* emit shortest paths made only from row equalities and edge reversal;
* deduplicate and subsumption-minimize the resulting row-choice nogoods over
  all twelve pin/deletion placements; and
* replay the finite search with the static nogood bank in place of dynamic
  metric-core detection.

All conclusions are exact only within the declared finite `(6,4,4)`, `n = 11`
incidence model.  The emitted JSON is computation evidence, not a Lean proof.
"""
