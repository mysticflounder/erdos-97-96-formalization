def _normalized_edge(edge: Edge) -> Edge | None:
    left, right = edge
    if left == right:
        return None
    return (left, right) if left < right else (right, left)


def _obligations_hold(
    rows: Sequence[metric.MetricRow],
    n: int,
    obligations: Sequence[Obligation],
) -> bool:
    closure = metric._row_equality_closure(n, rows)
    for _field, first, last in obligations:
        if first == last:
            continue
        first_edge = _normalized_edge(first)
        last_edge = _normalized_edge(last)
        if first_edge is None or last_edge is None:
            return False
        if closure.find(first_edge) != closure.find(last_edge):
            return False
    return True


def _required_rows(
    rows: Sequence[metric.MetricRow],
    stage: str,
    core: Mapping[str, Any],
) -> frozenset[RowKey]:
    if stage != "equality-exact-off-circle":
        return frozenset()
    wanted = (
        int(core["center"]),
        tuple(sorted(int(point) for point in core["support"])),
        True,
    )
    available = {_row_key(row) for row in rows}
    if wanted not in available:
        raise AssertionError("exact-off-circle core lost its exact source row")
    return frozenset((wanted,))


def _minimum_core_rows(
    rows: Sequence[metric.MetricRow],
    n: int,
    stage: str,
    core: Mapping[str, Any],
) -> tuple[tuple[metric.MetricRow, ...], dict[str, Any]]:
    """Find an exact minimum-cardinality set of whole row choices."""

    ordered = tuple(sorted(rows, key=_row_key))
    obligations = _core_obligations(stage, core)
    if not _obligations_hold(ordered, n, obligations):
        raise AssertionError("detector core obligations do not hold in its prefix")
    required = _required_rows(ordered, stage, core)
    required_indices = {
        index for index, row in enumerate(ordered) if _row_key(row) in required
    }
    optional_indices = tuple(
        index for index in range(len(ordered)) if index not in required_indices
    )
    tested = 0
    solutions: list[tuple[int, ...]] = []
    for optional_count in range(len(optional_indices) + 1):
        for optional in combinations(optional_indices, optional_count):
            indices = tuple(sorted((*required_indices, *optional)))
            tested += 1
            candidate = tuple(ordered[index] for index in indices)
            if _obligations_hold(candidate, n, obligations):
                solutions.append(indices)
        if solutions:
            break
    if not solutions:
        raise AssertionError("exhaustive row-subset search lost the detected core")
    selected = tuple(ordered[index] for index in solutions[0])
    return selected, {
        "criterion": (
            "minimum number of whole concrete center/support row choices "
            "preserving every recorded closure obligation"
        ),
        "method": "exhaustive subsets in increasing cardinality",
        "source_row_count": len(ordered),
        "minimum_row_count": len(selected),
        "optimal_subset_count": len(solutions),
        "subsets_tested": tested,
    }


def _closure_graph(
    rows: Sequence[metric.MetricRow], n: int
) -> dict[Edge, list[tuple[Edge, dict[str, Any]]]]:
    graph: dict[Edge, list[tuple[Edge, dict[str, Any]]]] = {
        (left, right): [] for left in range(n) for right in range(n)
    }
    for row in sorted(rows, key=_row_key):
        support = tuple(sorted(row.support))
        for first in support:
            for second in support:
                if first == second:
                    continue
                step = {
                    "kind": "row",
                    "center": row.center,
                    "first": first,
                    "second": second,
                }
                graph[(row.center, first)].append(((row.center, second), step))
    for first in range(n):
        for second in range(n):
            if first == second:
                continue
            step = {"kind": "flip", "first": first, "second": second}
            graph[(first, second)].append(((second, first), step))
    for node in graph:
        graph[node].sort(
            key=lambda item: (item[0], _canonical_json(item[1]))
        )
    return graph


def _closure_path(
    rows: Sequence[metric.MetricRow], n: int, first: Edge, last: Edge
) -> dict[str, Any]:
    if first == last:
        return {
            "first": list(first),
            "last": list(last),
            "steps": [],
            "length": 0,
            "row_equality_steps": 0,
        }
    graph = _closure_graph(rows, n)
    queue = deque((first,))
    visited = {first}
    previous: dict[Edge, tuple[Edge, dict[str, Any]]] = {}
    while queue and last not in visited:
        node = queue.popleft()
        for next_node, step in graph[node]:
            if next_node in visited:
                continue
            visited.add(next_node)
            previous[next_node] = node, step
            queue.append(next_node)
    if last not in visited:
        raise AssertionError(f"no direct row-equality path from {first} to {last}")
    steps = []
    node = last
    while node != first:
        node, step = previous[node]
        steps.append(step)
    steps.reverse()
    return {
        "first": list(first),
        "last": list(last),
        "steps": steps,
        "length": len(steps),
        "row_equality_steps": sum(step["kind"] == "row" for step in steps),
    }


def _validate_closure_path(
    rows: Sequence[metric.MetricRow], path: Mapping[str, Any]
) -> bool:
    by_center = {row.center: frozenset(row.support) for row in rows}
    current = tuple(int(value) for value in path["first"])
    for step in path["steps"]:
        if step["kind"] == "row":
            center = int(step["center"])
            first = int(step["first"])
            second = int(step["second"])
            if current != (center, first):
                return False
            if first not in by_center.get(center, ()) or second not in by_center.get(
                center, ()
            ):
                return False
            current = center, second
        elif step["kind"] == "flip":
            first = int(step["first"])
            second = int(step["second"])
            if current != (first, second):
                return False
            current = second, first
        else:
            return False
    return current == tuple(int(value) for value in path["last"])


def _side_conditions(stage: str, core: Mapping[str, Any]) -> list[dict[str, Any]]:
    if stage == "equality-exact-off-circle":
        return [
            {
                "kind": "exact-row",
                "center": int(core["center"]),
                "support": list(core["support"]),
                "off_support": int(core["off_support"]),
            }
        ]
    if stage == "equality-perpendicular-bisector-convex":
        return [{"kind": "convex-independent-carrier"}]
    if "convex-five-point" in stage or "convex-rhombus" in stage:
        return [
            {
                "kind": "canonical-cyclic-order",
                "orientation": "reverse" if stage.endswith("-reverse") else "forward",
            }
        ]
    return [{"kind": "injective-realization"}]


def _certificate_for_detection(
    rows: Sequence[metric.MetricRow],
    n: int,
    detection: Mapping[str, Any],
) -> dict[str, Any]:
    stage = str(detection["stage"])
    core = dict(detection["core"])
    selected, minimum = _minimum_core_rows(rows, n, stage, core)
    obligations = _core_obligations(stage, core)
    paths = []
    for field, first, last in obligations:
        path = _closure_path(selected, n, first, last)
        if not _validate_closure_path(selected, path):
            raise AssertionError("generated closure path failed direct replay")
        paths.append({"field": field, **path})
    key = _rows_key(selected)
    certificate = {
        "nogood_sha256": _nogood_sha256(key),
        "rows": _rows_json(key),
        "stage": stage,
        "core": core,
        "side_conditions": _side_conditions(stage, core),
        "closure_paths": paths,
        "minimum": minimum,
        "max_closure_path_length": max(
            (path["length"] for path in paths), default=0
        ),
        "direct_row_equality_replay": True,
    }
    certificate["proof_sha256"] = _sha256_json(certificate)
    return certificate


class _NogoodBank:
    """A deterministic antichain indexed by each nogood's least row choice."""

    def __init__(self) -> None:
        self.active: dict[NogoodKey, dict[str, Any]] = {}
        self.by_anchor: dict[RowKey, set[NogoodKey]] = defaultdict(set)
        self.empty: NogoodKey | None = None

    def _remove(self, key: NogoodKey) -> None:
        del self.active[key]
        if key:
            anchor = key[0]
            self.by_anchor[anchor].remove(key)
            if not self.by_anchor[anchor]:
                del self.by_anchor[anchor]
        else:
            self.empty = None

    def match(self, prefix: NogoodKey) -> NogoodKey | None:
        if self.empty is not None:
            return self.empty
        prefix_set = frozenset(prefix)
        candidates = {
            nogood
            for row in prefix
            for nogood in self.by_anchor.get(row, ())
            if len(nogood) <= len(prefix) and frozenset(nogood) <= prefix_set
        }
        return min(candidates, key=lambda key: (len(key), key)) if candidates else None

    def add(
        self, key: NogoodKey, certificate: Mapping[str, Any]
    ) -> tuple[NogoodKey, tuple[NogoodKey, ...], bool]:
        existing = self.match(key)
        if existing is not None:
            return existing, (), False
        key_set = frozenset(key)
        superseded = tuple(
            sorted(
                old
                for old in self.active
                if len(key) < len(old) and key_set < frozenset(old)
            )
        )
        for old in superseded:
            self._remove(old)
        stored = dict(certificate)
        self.active[key] = stored
        if key:
            self.by_anchor[key[0]].add(key)
        else:
            self.empty = key
        return key, superseded, True


class _PrefixRecorder:
    def __init__(self, frame: mc.Frame, order: Sequence[int]) -> None:
        self.frame = frame
        self.order = tuple(order)
        self.bank = _NogoodBank()
        self.certificates: dict[NogoodKey, dict[str, Any]] = {}
        self.usage: dict[NogoodKey, Counter[str]] = defaultdict(Counter)
        self.placements: dict[NogoodKey, set[str]] = defaultdict(set)
        self.cache: dict[NogoodKey, tuple[str, NogoodKey | None]] = {}
        self.counts: Counter[str] = Counter()
        self.detector_stages: Counter[str] = Counter()
        self.certificate_stages: Counter[str] = Counter()
        self.kill_depths: Counter[int] = Counter()
        self.placement_id = ""

    def _record_certificate_use(
        self,
        key: NogoodKey,
        *,
        origin: str,
        unique: bool,
        cache_hit: bool,
    ) -> None:
        usage = self.usage[key]
        usage[f"{origin}_kill_calls"] += 1
        if unique:
            usage[f"unique_{origin}_kills"] += 1
        if cache_hit:
            usage[f"cached_{origin}_kill_calls"] += 1
        if self.placement_id:
            self.placements[key].add(self.placement_id)
        stage = str(self.certificates[key]["stage"])
        self.certificate_stages[stage] += 1

    def _record_detection(
        self,
        rows: Sequence[metric.MetricRow],
        detection: Mapping[str, Any],
        *,
        origin: str,
    ) -> NogoodKey:
        self.detector_stages[str(detection["stage"])] += 1
        certificate = _certificate_for_detection(rows, self.frame.n, detection)
        candidate_key = _key_from_rows_json(certificate["rows"])
        if candidate_key not in self.certificates:
            self.certificates[candidate_key] = certificate
        chosen, superseded, inserted = self.bank.add(
            candidate_key, self.certificates[candidate_key]
        )
        self.counts["detector_certificates"] += 1
        self.counts["bank_insertions"] += int(inserted)
        self.counts["online_subsumed_certificates"] += len(superseded)
        return chosen

    def prefix_compatible(self, class_rows: Sequence[ClassRow]) -> bool:
        self.counts["prefix_calls"] += 1
        rows = direct._metric_rows(class_rows)
        prefix = _rows_key(rows)
        cached = self.cache.get(prefix)
        if cached is not None:
            self.counts["prefix_cache_hits"] += 1
            kind, key = cached
            if kind == "accept":
                return True
            self.counts["prefix_kill_calls"] += 1
            if kind == "metric":
                assert key is not None
                self._record_certificate_use(
                    key, origin="prefix", unique=False, cache_hit=True
                )
            else:
                self.counts["uncertified_prefix_kill_calls"] += 1
            return False

        self.counts["prefix_cache_misses"] += 1
        if not _cap_selected_rows_ok(rows, self.frame):
            self.cache[prefix] = ("cap-selected", None)
            self.counts["prefix_kill_calls"] += 1
            self.counts["unique_prefix_kills"] += 1
            self.counts["cap_selected_prefix_kills"] += 1
            self.counts["uncertified_prefix_kill_calls"] += 1
            self.kill_depths[len(prefix)] += 1
            return False

        matched = self.bank.match(prefix)
        if matched is not None:
            self.cache[prefix] = ("metric", matched)
            self.counts["prefix_kill_calls"] += 1
            self.counts["unique_prefix_kills"] += 1
            self.counts["bank_matched_prefix_kills"] += 1
            self.kill_depths[len(prefix)] += 1
            self._record_certificate_use(
                matched, origin="prefix", unique=True, cache_hit=False
            )
            return False

        detection = metric._formalized_metric_core(
            rows,
            self.frame.n,
            self.order,
            include_extended=False,
        )
        if detection is None:
            self.cache[prefix] = ("accept", None)
            return True

        chosen = self._record_detection(rows, detection, origin="prefix")
        self.cache[prefix] = ("metric", chosen)
        self.counts["prefix_kill_calls"] += 1
        self.counts["unique_prefix_kills"] += 1
        self.counts["detector_prefix_kills"] += 1
        self.kill_depths[len(prefix)] += 1
        self._record_certificate_use(
            chosen, origin="prefix", unique=True, cache_hit=False
        )
        return False

    def complete_compatible(self, class_rows: Sequence[ClassRow]) -> bool:
        self.counts["complete_calls"] += 1
        rows = direct._metric_rows(class_rows)
        if not _cap_selected_rows_ok(rows, self.frame):
            self.counts["complete_cap_selected_kills"] += 1
            return False
        detection = metric._formalized_metric_core(
            rows, self.frame.n, self.order
        )
        if detection is not None:
            chosen = self._record_detection(rows, detection, origin="complete")
            self._record_certificate_use(
                chosen, origin="complete", unique=True, cache_hit=False
            )
            self.counts["complete_metric_core_kills"] += 1
            return False
        self.counts["complete_core_free"] += 1
        return True

    def certificate_records(self) -> list[dict[str, Any]]:
        records = []
        for key in sorted(self.certificates, key=lambda item: (len(item), item)):
            records.append(
                {
                    "certificate": self.certificates[key],
                    "usage": dict(sorted(self.usage[key].items())),
                    "placements": sorted(self.placements[key]),
                    "active_after_placement": key in self.bank.active,
                }
            )
        return records


def _placement_id(pin_source_index: int, deleted_index: int) -> str:
    return f"pin-index-{pin_source_index}-deleted-index-{deleted_index}"


def _discovery_placement_job(args: tuple[int, int, int]) -> dict[str, Any]:
    pin_source_index, deleted_index, max_nodes = args
    started = time.perf_counter()
    labels = packet_labels(
        11,
        pin_source_index=pin_source_index,
        deleted_index=deleted_index,
    )
    frame, candidates = mc.cached_candidate_lists(labels.profile, set())
    assert candidates is not None
    pinned = ClassRow("pinned", PIN_CENTER, labels.pin_class, exact=True)
    variable_rows = direct._cap_filtered_rows(frame, candidates, labels.deleted)
    recorder = _PrefixRecorder(frame, hull_order(frame))
    recorder.placement_id = _placement_id(pin_source_index, deleted_index)

    result = solve_rows(
        frame,
        set(),
        fixed_rows=(pinned,),
        variable_rows=variable_rows,
        max_nodes=max_nodes,
        want_assignment=True,
        prefix_compatible=recorder.prefix_compatible,
        complete_compatible=recorder.complete_compatible,
    )
    status = result["status"]
    if status == "UNSAT":
        status = "EXHAUSTIVE"
    output = {
        "placement_id": recorder.placement_id,
        "pin_source_index": pin_source_index,
        "deleted_index": deleted_index,
        "pin_source": labels.pin_source,
        "deleted": labels.deleted,
        "status": status,
        "nodes": result["nodes"],
        "counts": dict(sorted(recorder.counts.items())),
        "detector_stage_counts": dict(sorted(recorder.detector_stages.items())),
        "certificate_stage_use_counts": dict(
            sorted(recorder.certificate_stages.items())
        ),
        "prefix_kill_depth_histogram": {
            str(depth): count for depth, count in sorted(recorder.kill_depths.items())
        },
        "certificate_records": recorder.certificate_records(),
        "elapsed_sec": time.perf_counter() - started,
    }
    if "assignment" in result:
        output["uncovered_assignment"] = result["assignment"]
    return output


def _merge_certificates(
    results: Sequence[Mapping[str, Any]],
) -> tuple[
    dict[NogoodKey, dict[str, Any]],
    dict[NogoodKey, Counter[str]],
    dict[NogoodKey, set[str]],
]:
    certificates: dict[NogoodKey, dict[str, Any]] = {}
    usages: dict[NogoodKey, Counter[str]] = defaultdict(Counter)
    placements: dict[NogoodKey, set[str]] = defaultdict(set)
    for result in results:
        for record in result["certificate_records"]:
            certificate = dict(record["certificate"])
            key = _key_from_rows_json(certificate["rows"])
            current = certificates.get(key)
            if current is None or _canonical_json(certificate) < _canonical_json(current):
                certificates[key] = certificate
            usages[key].update(record["usage"])
            placements[key].update(record["placements"])
    return certificates, usages, placements


def _subsumption_minimize(
    certificates: Mapping[NogoodKey, Mapping[str, Any]],
) -> tuple[tuple[NogoodKey, ...], dict[NogoodKey, NogoodKey]]:
    bank = _NogoodBank()
    cover: dict[NogoodKey, NogoodKey] = {}
    for key in sorted(certificates, key=lambda item: (len(item), item)):
        matched = bank.match(key)
        if matched is None:
            bank.add(key, certificates[key])
            cover[key] = key
        else:
            cover[key] = matched
    kept = tuple(sorted(bank.active, key=lambda item: (len(item), item)))
    return kept, cover


def _validate_certificate(certificate: Mapping[str, Any], n: int = 11) -> bool:
    key = _key_from_rows_json(certificate["rows"])
    if certificate["nogood_sha256"] != _nogood_sha256(key):
        return False
    payload = dict(certificate)
    proof_sha256 = payload.pop("proof_sha256", None)
    if proof_sha256 != _sha256_json(payload):
        return False
    rows = _metric_rows_from_key(key)
    obligations = _core_obligations(
        str(certificate["stage"]), certificate["core"]
    )
    if not _obligations_hold(rows, n, obligations):
        return False
    paths = certificate["closure_paths"]
    if len(paths) != len(obligations):
        return False
    for path, (field, first, last) in zip(paths, obligations):
        if path["field"] != field:
            return False
        expected = _closure_path(rows, n, first, last)
        if {key: path[key] for key in expected} != expected:
            return False
        if not _validate_closure_path(rows, path):
            return False
    return bool(certificate["direct_row_equality_replay"])


def _replay_placement_job(
    args: tuple[int, int, int, tuple[NogoodKey, ...]]
) -> dict[str, Any]:
    pin_source_index, deleted_index, max_nodes, nogoods = args
    started = time.perf_counter()
    labels = packet_labels(
        11,
        pin_source_index=pin_source_index,
        deleted_index=deleted_index,
    )
    frame, candidates = mc.cached_candidate_lists(labels.profile, set())
    assert candidates is not None
    pinned = ClassRow("pinned", PIN_CENTER, labels.pin_class, exact=True)
    variable_rows = direct._cap_filtered_rows(frame, candidates, labels.deleted)
    bank = _NogoodBank()
    for key in nogoods:
        bank.add(key, {"rows": _rows_json(key)})
    cache: dict[NogoodKey, NogoodKey | bool] = {}
    counts: Counter[str] = Counter()

    def prefix_compatible(class_rows: Sequence[ClassRow]) -> bool:
        counts["prefix_calls"] += 1
        rows = direct._metric_rows(class_rows)
        prefix = _rows_key(rows)
        if prefix in cache:
            counts["prefix_cache_hits"] += 1
            cached = cache[prefix]
            return cached is True
        counts["prefix_cache_misses"] += 1
        if not _cap_selected_rows_ok(rows, frame):
            counts["cap_selected_kills"] += 1
            cache[prefix] = False
            return False
        matched = bank.match(prefix)
        if matched is None:
            cache[prefix] = True
            return True
        counts["nogood_kills"] += 1
        cache[prefix] = matched
        return False

    complete_calls = 0

    def complete_compatible(_rows: Sequence[ClassRow]) -> bool:
        nonlocal complete_calls
        complete_calls += 1
        return True

    result = solve_rows(
        frame,
        set(),
        fixed_rows=(pinned,),
        variable_rows=variable_rows,
        max_nodes=max_nodes,
        want_assignment=True,
        prefix_compatible=prefix_compatible,
        complete_compatible=complete_compatible,
    )
    output = {
        "placement_id": _placement_id(pin_source_index, deleted_index),
        "pin_source_index": pin_source_index,
        "deleted_index": deleted_index,
        "pin_source": labels.pin_source,
        "deleted": labels.deleted,
        "status": result["status"],
        "nodes": result["nodes"],
        "complete_calls": complete_calls,
        "counts": dict(sorted(counts.items())),
        "elapsed_sec": time.perf_counter() - started,
    }
    if "assignment" in result:
        output["uncovered_assignment"] = result["assignment"]
    return output


def _run_jobs(
    jobs: Sequence[tuple], worker: Any, workers: int, label: str
) -> list[dict[str, Any]]:
    results = []
    if workers == 1:
        for index, job in enumerate(jobs, start=1):
            result = worker(job)
            results.append(result)
            print(
                f"[{label} {index}/{len(jobs)}] {result['placement_id']}: "
                f"{result['status']} nodes={result['nodes']}",
                flush=True,
            )
    else:
        with ProcessPoolExecutor(max_workers=workers) as pool:
            futures = {pool.submit(worker, job): job for job in jobs}
            for index, future in enumerate(as_completed(futures), start=1):
                result = future.result()
                results.append(result)
                print(
                    f"[{label} {index}/{len(jobs)}] {result['placement_id']}: "
                    f"{result['status']} nodes={result['nodes']}",
                    flush=True,
                )
    results.sort(key=lambda row: (row["pin_source_index"], row["deleted_index"]))
    return results


def _placement_pairs(
    placements: Sequence[tuple[int, int]] | None,
) -> tuple[tuple[int, int], ...]:
    if placements is None:
        return tuple(
            (pin, deleted)
            for pin in range(4)
            for deleted in range(4)
            if pin != deleted
        )
    normalized = tuple(sorted(set(placements)))
    for pin, deleted in normalized:
        if pin not in range(4) or deleted not in range(4) or pin == deleted:
            raise ValueError("placements must be distinct indices in 0..3")
    return normalized


def run(
    *,
    workers: int,
    max_nodes: int,
    placements: Sequence[tuple[int, int]] | None = None,
    replay: bool = True,
) -> dict[str, Any]:
    pairs = _placement_pairs(placements)
    discovery_jobs = [(pin, deleted, max_nodes) for pin, deleted in pairs]
    discovery_results = _run_jobs(
        discovery_jobs, _discovery_placement_job, workers, "discover"
    )
    certificates, usages, occurrence_placements = _merge_certificates(
        discovery_results
    )
    kept, cover = _subsumption_minimize(certificates)

    final_usages: dict[NogoodKey, Counter[str]] = defaultdict(Counter)
    final_placements: dict[NogoodKey, set[str]] = defaultdict(set)
    final_subsumed_counts: Counter[NogoodKey] = Counter()
    for key in certificates:
        representative = cover[key]
        final_usages[representative].update(usages[key])
        final_placements[representative].update(occurrence_placements[key])
        final_subsumed_counts[representative] += 1

    invalid = [key for key in kept if not _validate_certificate(certificates[key])]
    if invalid:
        raise AssertionError(f"{len(invalid)} final nogood certificates failed replay")

    final_bank = []
    for key in kept:
        entry = dict(certificates[key])
        entry["usage"] = dict(sorted(final_usages[key].items()))
        entry["placements"] = sorted(final_placements[key])
        entry["deduplicated_nogoods_covered"] = final_subsumed_counts[key]
        final_bank.append(entry)

    replay_results: list[dict[str, Any]] = []
    if replay:
        replay_jobs = [
            (pin, deleted, max_nodes, kept) for pin, deleted in pairs
        ]
        replay_results = _run_jobs(
            replay_jobs, _replay_placement_job, workers, "replay"
        )

    discovery_counts = Counter(
        result["status"] for result in discovery_results
    )
    replay_counts = Counter(result["status"] for result in replay_results)
    aggregate_counts: Counter[str] = Counter()
    detector_stages: Counter[str] = Counter()
    for result in discovery_results:
        aggregate_counts.update(result["counts"])
        detector_stages.update(result["detector_stage_counts"])

    row_histogram = Counter(len(key) for key in kept)
    max_path = max(
        (certificate["max_closure_path_length"] for certificate in final_bank),
        default=0,
    )
    prefix_kill_calls = aggregate_counts["prefix_kill_calls"]
    certified_prefix_kill_calls = (
        prefix_kill_calls - aggregate_counts["uncertified_prefix_kill_calls"]
    )
    all_replay_unsat = bool(replay_results) and all(
        result["status"] == "UNSAT" and result["complete_calls"] == 0
        for result in replay_results
    )
    source = Path(__file__).resolve()
    summary = {
        "claim_scope": MODEL_LABEL,
        "placements": len(pairs),
        "discovery_status_counts": dict(sorted(discovery_counts.items())),
        "replay_status_counts": dict(sorted(replay_counts.items())),
        "prefix_kill_calls": prefix_kill_calls,
        "unique_prefix_kills": aggregate_counts["unique_prefix_kills"],
        "certified_prefix_kill_calls": certified_prefix_kill_calls,
        "uncertified_prefix_kill_calls": aggregate_counts[
            "uncertified_prefix_kill_calls"
        ],
        "detector_certificate_occurrences": aggregate_counts[
            "detector_certificates"
        ],
        "deduplicated_nogood_count": len(certificates),
        "subsumption_minimal_nogood_count": len(kept),
        "subsumed_nogood_count": len(certificates) - len(kept),
        "minimum_row_count_histogram": {
            str(size): count for size, count in sorted(row_histogram.items())
        },
        "detector_stage_counts": dict(sorted(detector_stages.items())),
        "maximum_closure_path_length": max_path,
        "every_prefix_kill_has_direct_row_equality_certificate": (
            prefix_kill_calls > 0
            and certified_prefix_kill_calls == prefix_kill_calls
            and all(
                certificate["direct_row_equality_replay"]
                for certificate in final_bank
            )
        ),
        "static_nogood_bank_replays_every_placement": all_replay_unsat,
    }
    artifact = {
        "schema": SCHEMA,
        "generated_at": datetime.now(timezone.utc).isoformat(),
        "source": os.path.relpath(source, ROOT),
        "source_sha256": hashlib.sha256(source.read_bytes()).hexdigest(),
        "dependency_sha256": {
            os.path.relpath(path, ROOT): hashlib.sha256(path.read_bytes()).hexdigest()
            for path in DEPENDENCIES
        },
        "scope": {
            "claim_scope": MODEL_LABEL,
            "n": 11,
            "profile": [6, 4, 4],
            "placements": [list(pair) for pair in pairs],
            "max_nodes_per_placement": max_nodes,
            "workers": workers,
            "row_choice_unit": "one exact center/support choice",
            "minimum_definition": "minimum cardinality among whole row choices",
            "closure_steps": ["direct-row-equality", "edge-reversal"],
            "uses_dynamic_metric_detector_only_during_discovery": True,
            "static_replay_requested": replay,
        },
        "summary": summary,
        "discovery_results": [
            {key: value for key, value in result.items() if key != "certificate_records"}
            for result in discovery_results
        ],
        "replay_results": replay_results,
        "nogood_bank_sha256": _sha256_json(final_bank),
        "nogoods": final_bank,
    }
    return artifact


def _parse_placement(value: str) -> tuple[int, int]:
    try:
        pin_text, deleted_text = value.split(":", maxsplit=1)
        return int(pin_text), int(deleted_text)
    except (ValueError, TypeError) as exc:
        raise argparse.ArgumentTypeError("placement must be PIN_INDEX:DELETED_INDEX") from exc


def main() -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--out", type=Path)
    parser.add_argument("--workers", type=int, default=min(8, os.cpu_count() or 1))
    parser.add_argument("--max-nodes", type=int, default=DEFAULT_MAX_NODES)
    parser.add_argument(
        "--placement",
        action="append",
        type=_parse_placement,
        dest="placements",
        help="restrict to PIN_INDEX:DELETED_INDEX; repeat as needed",
    )
    parser.add_argument(
        "--no-replay",
        action="store_true",
        help="skip the independent static-bank DFS replay",
    )
    args = parser.parse_args()
    if args.workers < 1:
        parser.error("--workers must be positive")
    if args.max_nodes < 1:
        parser.error("--max-nodes must be positive")
    try:
        artifact = run(
            workers=args.workers,
            max_nodes=args.max_nodes,
            placements=args.placements,
            replay=not args.no_replay,
        )
    except ValueError as exc:
        parser.error(str(exc))
    if args.out is not None:
        args.out.parent.mkdir(parents=True, exist_ok=True)
        args.out.write_text(json.dumps(artifact, indent=2, sort_keys=True) + "\n")
        print(f"wrote {args.out}")
    print(json.dumps(artifact["summary"], indent=2, sort_keys=True))
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
